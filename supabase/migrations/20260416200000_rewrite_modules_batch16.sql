**THE WIDER AGAVE WORLD**

Tequila gets all the attention, but Mexico produces several agave spirits — each with a distinct identity.

**MEZCAL** — any agave spirit from 9 Mexican states (mostly Oaxaca). Agave is PIT-ROASTED underground, giving the signature smoke. Must be 100% agave.
• **Espadín** — the workhorse agave (~90% of mezcal). Smoke, citrus, mineral.
• **Tobalá** — wild-harvested, floral, complex. $60+.
• **Top producers:** Del Maguey (Vida for mixing, Chichicapa for sipping), Montelobos, Bozal, Ilegal.

**RAICILLA** — from Jalisco. Different agave, different production. Funky, tropical, less smoky. "Jalisco's wild moonshine."

**SOTOL** — NOT technically agave — it's from the Dasylirion plant. From Chihuahua. Herbal, grassy, minty.

**BACANORA** — from Sonora. Agave Pacifica. Drier, mineral-driven.

**TOP SELLERS**

1. **Del Maguey Vida** — $35. "THE gateway mezcal. Smokiest of the common bottles."
2. **Montelobos Espadín** — $32. "Gentler smoke for mezcal beginners."
3. **Ilegal Mezcal Joven** — $35. "The bartender's mezcal."
4. **Del Maguey Chichicapa** — $60. "The sipping mezcal."
5. **Bozal Ensamble** — $45. "Multi-agave blend. Complex."

**WHAT TO SAY:** "Want to try mezcal? Del Maguey Vida, $35. Start there. Smoky Margarita = split 1 oz mezcal + 1 oz tequila."
