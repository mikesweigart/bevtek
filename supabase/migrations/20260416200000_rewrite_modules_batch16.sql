• **Heineken 0.0** — mainstream name recognition. "The one everyone's heard of."
• **Guinness 0** — remarkably close to real Guinness. "For the stout lover going dry."
• **Partake** — ultra-low calorie (10-30 per can). "For the health-focused."

**NA SPIRITS**

• **Seedlip** — the pioneer. Botanical distillates. "NA G&T made with Garden 108."
• **Lyre's** — broadest range. Italian Orange = NA Aperol Spritz. "One-for-one cocktail substitutes."
