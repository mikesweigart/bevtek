**THE SIMPLE VERSION**

Beer pairing follows wine pairing rules: match intensity, complement or contrast, think regionally. The bonus: beer has carbonation AND bitterness, which are BOTH palate-cleansers. Beer can often match food even better than wine.

**THE CHEAT SHEET**

• **Spicy food (Indian, Thai, Mexican)** → IPA. Hops cut through capsaicin. "The #1 beer pairing."
• **Fried food** → Pilsner, lager, or sour. Carbonation + acid = grease-cutting.
• **Grilled meat / BBQ** → Amber, Zinfandel-like malty beers, or strong IPAs.
• **Steak** → Imperial Stout or malty amber. Roast + roast = harmony.
• **Oysters** → Guinness (or any dry stout). "Legendary pairing. Try it to believe it."
• **Sushi** → Japanese lager (Sapporo, Asahi). Or dry pilsner.
• **Cheese** → Belgian ales. Tripel + Brie. Dubbel + Gouda.
• **Chocolate dessert** → Imperial Stout, Porter. Dark + dark.
• **Pizza** → Pale ale, amber, pilsner. "Anything with bubbles and moderate hops."

**THE UNIVERSAL RULE:** "Carbonation and bitterness do the same job as acidity in wine — they cut richness and refresh the palate."

**WHAT TO SAY:** "What are you eating? That IPA would be amazing with the tacos."
