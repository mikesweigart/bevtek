**THE $500M CATEGORY**

NA spirits barely existed in 2018. By 2026, the category is $500M+. These are distilled or macerated botanical waters that mimic gin, whiskey, tequila, and aperitivos — without alcohol. Used for "mocktails" that look and feel like real cocktails.

**THE BIG THREE**

1. **Seedlip** ($30-35) — the pioneer. Garden 108 (herbal, sub for gin), Spice 94 (warm, sub for whiskey), Grove 42 (citrus, sub for vodka).
2. **Lyre's** ($25-35) — broadest range. Italian Orange (Aperol sub), American Malt (bourbon sub), Dry London (gin sub). "One-for-one cocktail substitutes."
3. **Monday** ($25-30) — US-based. Monday Gin, Whiskey, Mezcal. "Clean, approachable."

**WHERE TO STOCK:** Near cocktail mixers and premium spirits. Not in a "health" section. Position as "you can still enjoy cocktails" — not "you can't drink."

**PAIR WITH:** Fever-Tree tonic, Q Ginger Beer, premium sodas. "Seedlip + Fever-Tree tonic gift set — under $50."

**WHAT TO SAY:** "Sober-curious? Seedlip Garden + Fever-Tree tonic. Looks, feels, and tastes like a G&T."
