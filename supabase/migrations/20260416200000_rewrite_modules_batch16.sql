**THE PROBLEM**

Craft beer has exploded to 9,000+ breweries. For someone who's only ever had Bud Light, walking into a craft section is paralyzing. Your job: guide without overwhelming. Start simple, build up.

**THE 5 GATEWAY STYLES**

1. **Helles Lager** (Weihenstephaner, Augustiner) — "Better Bud Light." Crisp, clean, malty.
2. **Wheat Beer** (Hoegaarden, Allagash White) — "Blue Moon but better." Hazy, citrus.
3. **Amber / Red Ale** (Fat Tire, Killian's) — "Bridge to craft." Malty, caramel.
4. **Pale Ale** (Sierra Nevada, Dale's Pale) — "Hops 101." Hoppy but not overwhelming.
5. **Session IPA** (All Day IPA, DayTime) — "IPA at lunch ABV." Full flavor, 4-5%.

**THE DISCOVERY PATH:**
Lager → Wheat → Pale Ale → IPA → DIPA → Stout → Sour → Belgian

**DON'T START WITH:** Imperial Stout (too heavy), sour/lambic (too extreme), Triple IPA (too intense), barrel-aged anything (too complex + expensive for exploration).

**THE FORMAT:** Singles and mix-six packs. DON'T push a 6-pack of something they've never tried.

**WHAT TO SAY:** "What do you usually drink?" → suggest one step up in complexity. "Bud Light? Try this Helles Lager. Same easy-drinking, more flavor."
