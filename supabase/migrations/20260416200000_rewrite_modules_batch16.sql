**THE CONVENIENCE REVOLUTION**

RTDs (Ready-to-Drink) are the fastest-growing alcohol category. Pre-mixed, single-serve, ready to open. Three subcategories:

**Hard seltzer** — flavored sparkling water + alcohol. 100 cal, 5% ABV. White Claw, Truly, High Noon.

**Spirits-based canned cocktails** — real liquor in a can. Higher quality, 7-12% ABV. Cutwater, Tip Top, OTR.

**Malt-based** — cheaper base. Four Loko, Smirnoff Ice, Mike's Hard.

**TOP SELLERS**

1. **High Noon** — $12/4-pack. Vodka + real juice + soda. "#1 spirits-based RTD in America."
2. **White Claw Variety Pack** — $18/12-pack. "The hard seltzer everyone knows."
3. **Cutwater Margarita** — $14/4-pack. "Real tequila in a can."
4. **Tip Top Old Fashioned** — $16/4-pack. "Actual cocktail-bar quality."
5. **Modelo Chelada** — $12/3-pack. "Beer + tomato + lime. The picnic move."

**WHAT SELLS:** Variety packs outsell single-flavor. High Noon is the #1 spirits-based RTD.

**WHAT TO SAY:** "Pool party? High Noon variety pack. Tailgate? Cutwater Margarita. Gift? Tip Top Old Fashioned 4-pack — $16."
