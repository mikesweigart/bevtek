**AMERICA'S #1 COCKTAIL**

The Margarita is the most-ordered cocktail in America. Tequila + lime + orange liqueur. Simple, refreshing, endlessly riffable.

**THE CLASSIC:** 2 oz blanco tequila + 1 oz lime juice + ¾ oz Cointreau. Salt rim. Rocks glass.

**THE FAMILY:**
• **Tommy's** — agave syrup replaces Cointreau. Cleaner, tequila-forward.
• **Spicy** — muddled jalapeño, Tajín rim. The trending version.
• **Frozen** — blended with ice. Use less citrus.
• **Mezcal** — sub mezcal for tequila. Smoky twist.
• **Skinny** — tequila + lime + agave + soda. Fewer calories.
• **Top-shelf** — reposado tequila + Grand Marnier.

**THE NON-NEGOTIABLE:** Fresh lime juice. NEVER bottled.
**THE UPGRADE:** Cointreau over cheap triple sec. Night-and-day difference.

**TOP SELLERS FOR MARGARITAS**

