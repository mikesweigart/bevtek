**THE MOST ARGUED-ABOUT COCKTAIL IN HISTORY**

The Martini is simple — gin (or vodka) + vermouth, stirred, strained, garnished. But every element is debated. Gin or vodka? Dry or wet? Olive or twist? Shaken or stirred?

Bond got it wrong. Shaking a Martini creates cloudiness and over-dilution. Stirring produces the correct silky texture. But the customer is always right — if they want it shaken, shake it.

**THE CLASSIC DRY MARTINI**
• 2½ oz gin (London Dry: Tanqueray, Sipsmith) + ½ oz dry vermouth (Dolin, Noilly Prat)
• Stir 20 seconds with ice, strain into chilled coupe/martini glass
• Lemon twist or olive

**THE TERMS:**
• **"Dry"** = less vermouth. Classic is 5:1; very dry is 10:1.
• **"Wet"** = more vermouth. 2:1 is a "50/50 Martini." Modern bars trend wetter.
• **"Dirty"** = olive brine added. Usually vodka. ¼-½ oz brine.
• **"Gibson"** = Martini with a cocktail onion instead of olive.
• **"Vesper"** = Bond's: gin + vodka + Lillet Blanc.

**VERMOUTH MATTERS:** old vermouth = bad Martini. Refrigerate after opening. Use within 4-6 weeks.

**TOP SELLERS FOR MARTINIS**

1. **Tanqueray** — $22. "The benchmark Martini gin."
2. **Grey Goose** — $35. "The vodka Martini default."
3. **Dolin Dry Vermouth** — $15. "Light, clean, correct."
4. **Castelvetrano olives** — $8. "The green Sicilian olive. Buttery, mild."

**WHAT TO SAY:** "Martini at home? Tanqueray + Dolin Dry + olives. Under $45 total."
