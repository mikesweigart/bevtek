**THE MOST FORGIVING COCKTAIL TEMPLATE**

The sour template — spirit + citrus + sweet — is the foundation of half the cocktails in existence. Margarita = tequila sour. Daiquiri = rum sour. Sidecar = brandy sour. Master the Whiskey Sour and you understand the DNA of cocktails.

**THE CLASSIC:** 2 oz bourbon + ¾ oz lemon + ¾ oz simple. Optional: egg white for foam.

**THE FAMILY:**
• **Boston Sour** — with egg white. Silky foam top. Shake dry first (no ice), then with ice.
• **New York Sour** — float ½ oz red wine on top. Dramatic, delicious.
• **Gold Rush** — honey syrup instead of simple. Warmer, rounder.
• **Penicillin** — Scotch + honey-ginger syrup + lemon + peated Scotch float. "The modern classic."
• **Amaretto Sour** — amaretto + bourbon split + lemon. "Better than you expect."

**WHY IT'S FORGIVING:** Too much lemon? Sugar balances it. Too sweet? Lemon corrects it. Hard to make truly bad.

**THE EGG WHITE QUESTION:** Doesn't add egg flavor — acid neutralizes it. Creates creamy texture and visual foam. Aquafaba (chickpea water) is the vegan substitute.

**TOP SELLERS**

1. **Buffalo Trace** — $25. "The Whiskey Sour bourbon."
2. **Fresh lemons** — "Non-negotiable."
3. **Angostura bitters** — $10. "3 drops on the foam."
4. **Compass Box Peat Monster** — $55. "For the Penicillin cocktail."

**WHAT TO SAY:** "The Whiskey Sour is the cocktail that teaches everything. Master it and you understand 50% of all cocktails."
