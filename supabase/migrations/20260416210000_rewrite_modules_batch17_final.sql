**THE ORIGINAL COCKTAIL**

The Old Fashioned may be the world's oldest cocktail — dating to the early 1800s. The template: spirit + sugar + bitters + water. Three ingredients (plus ice and citrus), and yet every element matters. Get it right, and it's the most satisfying drink in the world.

**THE RECIPE**

• 2 oz bourbon or rye
• 1 sugar cube, ¼ oz simple syrup, or ¼ oz rich demerara syrup
• 2-3 dashes Angostura bitters
• Orange twist (expressed over the drink)
• Optional: Luxardo maraschino cherry
• Large ice cube, stirred gently in a rocks glass

**RYE vs. BOURBON:**
• **Rye** = classic NYC style. Drier, spicier, peppery. Rittenhouse, Sazerac Rye.
• **Bourbon** = modern Louisville style. Sweeter, rounder, vanilla. Buffalo Trace, Woodford Reserve.
Neither is wrong. Ask the customer which they prefer.

**SUGAR DEBATE:**
• **Demerara syrup** (2:1 turbinado sugar + water) = the craft bartender's choice. Caramelly, rich.
• **Simple syrup** (1:1 white sugar + water) = consistent, easy.
• **Sugar cube** = traditional. Muddle with bitters. Slightly inconsistent but romantic.

**THE MISTAKES TO AVOID:**
• Muddling the orange (adds bitter pith) — EXPRESS the oils instead (twist the peel over the drink)
• Adding muddled fruit salad (cherry + orange slice) — fine for a "Wisconsin Old Fashioned" but not the classic
• Shaking instead of stirring — always stir spirit-forward drinks

**TOP SELLERS FOR OLD FASHIONEDS**

1. **Buffalo Trace** — $25. "The default bourbon for OFs."
2. **Rittenhouse Rye** — $28. "The bartender's rye OF."
3. **Woodford Reserve** — $35. "The 'nice' Old Fashioned."
4. **Angostura Bitters** — $10. "Non-negotiable."
5. **Luxardo Cherries** — $22. "The cherry upgrade that changes everything."

**WHAT TO SAY:** "Making Old Fashioneds? You need: good bourbon ($25), Angostura bitters ($10), and Luxardo cherries ($22). Under $60 for the complete kit."
