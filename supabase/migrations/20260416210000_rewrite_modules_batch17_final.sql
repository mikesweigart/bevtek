**THE ITALIAN HAPPY HOUR**

Aperitivo is Italy's pre-dinner drinking tradition. Light, bitter, effervescent drinks that stimulate appetite. Low-alcohol (~8-11% ABV), sessionable, and the fastest-growing cocktail category in America.

**THE DRINKS:**

• **Aperol Spritz** — 3 Prosecco : 2 Aperol : 1 soda. Orange slice. Large wine glass. "THE summer drink."
• **Campari Spritz** — same but Campari. More bitter, more adult.
• **Hugo** — elderflower + Prosecco + soda + mint. Floral, refreshing.
• **Negroni Sbagliato** — Campari + vermouth + Prosecco. "Went viral in 2022."
• **Americano** — Campari + vermouth + soda. The Negroni's low-ABV ancestor.

**WHY IT MATTERS FOR YOUR STORE:** brunch staple, summer category, entry-level "sophisticated" cocktail, growing $B+ market.

**THE BUNDLE:** Aperol ($25) + Prosecco ($14) + oranges = "Spritz Kit" for $40.

