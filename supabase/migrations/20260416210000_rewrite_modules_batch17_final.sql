**THE SIMPLEST COCKTAILS THAT NEVER GO WRONG**

Highball = spirit + long mixer (soda, tonic, ginger beer). Served tall over ice. Simple, sessionable, food-friendly. The cocktails people actually make at home every day.

**THE ESSENTIALS:**

• **Gin & Tonic** — 2 oz gin + 4 oz tonic + lime. "Tonic quality matters — Fever-Tree is the upgrade."
• **Moscow Mule** — 2 oz vodka + 4 oz ginger beer + lime. Copper mug optional.
• **Dark 'n' Stormy** — 2 oz Gosling's dark rum + 4 oz ginger beer + lime.
• **Paloma** — 2 oz tequila + grapefruit soda + lime. "Mexico's actual favorite tequila drink."
• **Whiskey Highball** — 1 oz Japanese whisky + 4 oz cold soda. "The Tokyo way."
