**THE COCKTAIL WITH NO HIDING PLACE**

The Negroni is three ingredients in equal parts: gin, Campari, sweet vermouth. 1:1:1. Stirred, served over ice with an orange peel. It's been around since 1919 and has spawned an entire family of variations.

What makes the Negroni special is its transparency — every ingredient is exposed. Bad gin, stale vermouth, or too much Campari, and the drink falls apart. Get it right, and it's perfectly balanced: bitter-sweet-botanical.

**THE CLASSIC:** 1 oz gin + 1 oz Campari + 1 oz sweet vermouth. Stirred, rocks glass, orange peel.

**THE FAMILY:**
• **Sbagliato** — Prosecco instead of gin. Lighter, bubbly, brunch-friendly.
• **Boulevardier** — bourbon/rye instead of gin. Warmer, richer.
• **White Negroni** — gin + Suze + Lillet Blanc. For Campari-averse drinkers.
• **Mezcal Negroni** — mezcal instead of gin. Smoky brilliance.

**THE VERMOUTH RULE:** REFRIGERATE after opening. Vermouth is wine. It spoils in days at room temp. Months of stale vermouth ruin more Negronis than anything else.

**TOP SELLERS FOR NEGRONIS**

1. **Tanqueray Gin** — $22. "The default Negroni gin."
2. **Campari** — $28. "Non-negotiable. There is no substitute."
3. **Carpano Antica Sweet Vermouth** — $35. "THE upgrade. Transforms the Negroni."
4. **Cocchi Vermouth di Torino** — $22. "The budget alternative to Carpano that's still excellent."

**NEGRONI KIT:** Tanqueray + Campari + Carpano = $85. "The 3-bottle gift that impresses every cocktail lover."
