**THE ONE QUESTION THAT SPLITS THE SCOTCH WORLD**

"Do you like smoky?" This single question determines EVERYTHING about your Scotch recommendation. The entire category divides along this axis — smoky or not smoky — and there's no middle ground for most drinkers (they love peat or they don't).

**WHAT IS PEAT?**

Peat is decomposed vegetation — moss, heather, grass — compressed over thousands of years into dense, dark, burnable material. In Scotland's Islay region, peat is burned to dry malted barley before distillation. The smoke infuses the grain with phenolic compounds that create Scotch's signature smoky, maritime, sometimes medicinal character.

**PEAT LEVELS (measured in PPM — phenol parts per million)**

• **0-5 PPM** = unpeated (most Speyside, Highland)
• **5-20 PPM** = lightly peated (Highland Park, Talisker)
• **20-40 PPM** = moderately peated (Caol Ila, Bowmore)
• **40-60 PPM** = heavily peated (Laphroaig, Lagavulin, Ardbeg)
• **60+ PPM** = extreme (Bruichladdich Octomore — world's most peated)

**THE UNPEATED SIDE (no smoke)**

Sweet, fruity, approachable. Where 80% of Scotch drinkers live:
• **Glenlivet 12** — $45. Citrus, apple, vanilla. "The gateway."
• **Glenfiddich 12** — $45. Pear, honey, malt. "The best-seller."
• **Macallan 12 Sherry Oak** — $70. Rich, dried fruit, sherry. "The prestige name."
• **Balvenie 14 DoubleWood** — $65. Honey, dried fruit, oak. "The enthusiast's favorite."

**THE PEATED SIDE (smoky)**

Campfire, sea salt, iodine, leather. Intensely polarizing:
• **Highland Park 12** — $50. BALANCED smoke + honey. "The gateway to peat."
• **Talisker 10** — $55. Maritime, pepper, sea salt. "Ocean in a glass."
• **Lagavulin 16** — $85. Deep, complex, iconic. "The definitive peated Scotch."
• **Laphroaig 10** — $50. Medicinal, bandage, campfire. "Love-it-or-hate-it."
• **Ardbeg 10** — $55. Smoke + lemon + pepper. "Vibrant, exciting peat."

**THE GATEWAY STRATEGY**

Don't start a peat-curious customer with Laphroaig (too intense). Start with:
1. **Highland Park 12** — balanced smoke + honey
2. **Talisker 10** — maritime, peppery
3. THEN Lagavulin 16 → Laphroaig → Ardbeg

**WHAT TO SAY ON THE FLOOR**

"Do you like smoky? Yes → Lagavulin 16. No → Glenlivet 12. That's the whole category in one question."

"Want to TRY smoky? Highland Park 12 — it won't scare you off."

"'Scotch that tastes like a campfire on a beach' → Talisker 10."
