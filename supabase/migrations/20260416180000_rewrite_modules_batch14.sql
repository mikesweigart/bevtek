**BEYOND BOURBON 101**

You've learned what bourbon IS (51%+ corn, new charred oak, etc.). Now learn WHY different bourbons taste so different — and how to match the right bourbon to the right customer.

The secret is the MASH BILL — the grain recipe. All bourbon is majority corn, but the remaining 49% determines whether the bourbon is sweet and soft or spicy and assertive.

**THE THREE BOURBON FAMILIES**

**HIGH-RYE BOURBON (15-35% rye)**
Spicy, peppery, dry, assertive. Rye grain adds black pepper, baking spice, and a drying quality. These are "bartender's bourbons" — they hold their own in cocktails.
• Buffalo Trace — ~10% rye. The balanced starting point.
• Four Roses Small Batch — 20-35% rye (multiple recipes blended). Complex, floral.
• Bulleit Bourbon — 28% rye. One of the highest-rye bourbons made.
• Wild Turkey 101 — ~13% rye. Bold and spicy at 101 proof.
• Old Grand-Dad — 27% rye. The hipster's bourbon.

**WHEATED BOURBON (wheat replaces rye)**
Soft, sweet, round, gentle. Wheat adds honey, bread, and a creamy texture. These are "sipping bourbons" — best neat or on one rock.
• Maker's Mark — ~16% red winter wheat. The icon.
• Weller Special Reserve — same mash bill as Pappy Van Winkle.
• Larceny — Heaven Hill's wheated bourbon.
• Rebel — budget wheated option.

**HIGH-CORN (75%+ corn)**
Sweet, simple, candy-like, approachable. Often young and budget-friendly.
• Mellow Corn — 80% corn, 100 proof. $15. The internet's favorite.

**THE FLAVOR MAP**
More rye → drier, spicier, black pepper
More wheat → softer, sweeter, honey-bread
More corn → sweeter, simpler, candy-vanilla

**AGE: NOT ALWAYS BETTER**

• 4-6 years: youthful, bright, grain-forward
• 8-12 years: the sweet spot. Balanced oak + fruit + grain.
• 15+ years: heavy oak, tannic, can be bitter. NOT always better.

Bourbon ages FASTER than Scotch because Kentucky's hot summers push whiskey deeper into the wood. A 10-year bourbon has more oak impact than a 10-year Scotch.

**WHAT TO SAY ON THE FLOOR**

"Want smooth sipping? Wheated bourbon — Maker's Mark or Weller."

"Making cocktails? High-rye — Wild Turkey 101 or Four Roses. Holds up against mixers."

"What's the difference? Rye makes it spicy, wheat makes it soft. Both are bourbon."
