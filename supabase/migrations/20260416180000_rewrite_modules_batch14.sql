**THE STORY**

Riesling is the most misunderstood grape in your store — and arguably the most versatile food wine in the world. The problem: Americans assume ALL Riesling is sweet. It's not. Dry Riesling (Trocken) is piercingly crisp, bone-dry, and mineral. The misconception comes from the 1970s-80s when cheap, sweet German Riesling (Blue Nun, Black Tower) dominated the US market.

Today's best Rieslings range from razor-dry to lusciously sweet — and the LABEL tells you which, if you know how to read it.

**THE GERMAN LABEL DECODER**

• **Trocken** = DRY. This is the word that converts "I don't like sweet wine" customers.
• **Halbtrocken / Feinherb** = off-dry. Barely sweet. The spicy-food champion.
• **Kabinett** = light, often off-dry. 8-10% ABV. Featherweight.
• **Spätlese** = riper. Can be dry or sweet (depends on producer).
• **Auslese** = late harvest. Usually sweet. Rich.
• **Beerenauslese (BA) / Trockenbeerenauslese (TBA)** = dessert wine. Very rare, very expensive.
• **Eiswein** = ice wine. Intensely sweet + acid. Apricot, lychee.

**ALSACE (France):** Rieslings here are almost always DRY. Fuller-bodied than German. No sweetness codes needed.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Dr. Loosen Blue Slate Riesling** — $14. Mosel. Off-dry, peach, slate mineral. "The gateway Riesling. Under $15, drink-all-day quality."
2. **Trimbach Riesling** — $18. Alsace. Bone-dry, citrus, steely. "The DRY Riesling for skeptics."
3. **Selbach-Oster Kabinett** — $18. Mosel. Delicate, 8% ABV, apple-peach. "Light as air. Dangerous because you don't notice the alcohol."
4. **Hugel Riesling** — $16. Alsace. Classic, balanced, food-friendly.
5. **Chateau Ste. Michelle Riesling** — $10. Washington. Off-dry, approachable. "American Riesling under $10 that actually tastes good."

**WHAT TO SAY ON THE FLOOR**

"'I don't like sweet wine' → 'This one says TROCKEN — it's dry. Taste it.' Converts them every time."

"Spicy food? Off-dry Riesling. The sweetness calms the heat, the acid refreshes. Nothing else does this as well."

"Thai, Indian, Sichuan? Riesling. It's the #1 spicy-food wine on Earth."
