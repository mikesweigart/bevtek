**THE STORY**

Spain is the third-largest wine producer in the world and arguably the best VALUE in wine today. While Rioja gets all the attention, Spain's other regions produce extraordinary wines that most American customers have never heard of — at prices that make Napa weep.

**RIBERA DEL DUERO** — Tempranillo (called "Tinto Fino" here) at its most powerful. Think Bordeaux-meets-Spain: structured, dark, age-worthy. Vega Sicilia "Unico" ($400+) is Spain's most prestigious wine. Pesquera, Aalto, and Pago de los Capellanes deliver at $25-60.

**PRIORAT** — tiny, mountainous Catalonia. Old-vine Garnacha + Cariñena on slate. Massive concentration, mineral, wild. Álvaro Palacios makes the icon wines (L'Ermita $500+), but his Camins del Priorat is $15 — one of the best value wines in the world.

**RÍAS BAIXAS** — Albariño from Spain's Atlantic coast. Crisp, saline, citrus, almond. Born for shellfish. $15-25. Martín Códax, Do Ferreiro.

**CAVA** — sparkling, traditional method (like Champagne). $10-25. The best Champagne alternative for the money.

**SHERRY (Jerez)** — the most underrated wine category. Fino and Manzanilla (dry, salty, perfect aperitif). Pedro Ximénez (PX — insanely sweet, pour over ice cream). Sherry is a sommelier's obsession and a consumer's mystery.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Álvaro Palacios Camins del Priorat** — $15. "Probably the best $15 wine in the world."
2. **Martín Códax Albariño** — $14. Rías Baixas. "Spanish Sauv Blanc alternative for seafood."
3. **Segura Viudas Brut Cava** — $10. "Champagne-method sparkling for $10."
4. **Pesquera Crianza (Ribera del Duero)** — $25. "Spain's answer to fine Bordeaux."
5. **Lustau Fino Sherry** — $15. "The aperitif that sommeliers drink. Try it chilled."

**WHAT TO SAY ON THE FLOOR**

"Loves Napa Cab? Try Ribera del Duero — $25-40, same power, different terroir."

"Cheap Champagne alternative? Cava. Same method. $10."

"Seafood? Albariño. Spain's Atlantic coast white."
