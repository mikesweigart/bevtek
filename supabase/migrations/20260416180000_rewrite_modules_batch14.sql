**THE WORLD'S OLDEST LUXURY PAIRING**

Wine + cheese is the original pairing — older than wine + steak, older than beer + pizza. The science is simple: fat and protein in cheese coat the palate. Acid and tannin in wine cut through and refresh. Together, they create a cycle of pleasure that neither provides alone.

**THE THREE RULES**

1. **Match intensity.** Mild cheese (mozzarella, brie) with light wine (Pinot Grigio, rosé). Strong cheese (aged cheddar, Roquefort) with big wine (Cabernet, Port).

2. **"What grows together goes together."** French cheese + French wine. Italian + Italian. Spanish + Spanish. Centuries of co-evolution.

3. **Sweet + salty = magic.** Sauternes + Roquefort. Port + Stilton. The contrast is electric.

**THE DEFINITIVE CHEAT SHEET**

• **Aged Cheddar** → Cabernet Sauvignon, Zinfandel, aged Bourbon (yes, bourbon)
• **Brie / Camembert** → Champagne, Pinot Noir, Chardonnay
• **Goat Cheese (Chèvre)** → Sancerre (THE classic pairing)
• **Gruyère / Comté** → white Burgundy, Beaujolais, Alsace Pinot Gris
• **Parmigiano-Reggiano** → Chianti, Barolo, Lambrusco (the authentic local pairing!)
• **Blue Cheese (Stilton, Roquefort)** → Sauternes, Port, late-harvest Riesling
• **Manchego** → Rioja, Cava, Tempranillo
• **Epoisses / washed rind** → Gewürztraminer, Belgian ales

**WHAT TO AVOID**

• Very tannic reds + very creamy cheese = metallic taste
• Delicate whites + pungent blue cheese = the cheese overwhelms
• Don't overthink it. Most wine + most cheese = pretty good.

**THE SALES OPPORTUNITY**

Cheese-and-wine night is the easiest dinner party to sell. "Pick 4 cheeses + 4 wines. Done." Total customer spend: $60-100 in wine alone, plus they might buy crackers, honey, and charcuterie from the deli next door.

**TOP SELLERS FOR CHEESE BOARDS**

1. **Sancerre** — $25. For goat cheese. "The 800-year-old pairing."
2. **Champagne** — $45+. Brie, Camembert. "Bubbles + soft cheese = luxury."
3. **Port (Taylor Fladgate 20 Year)** — $40. Stilton. "The dessert course."
4. **Chianti Classico** — $20. Parmigiano. "The Italian board."
5. **Rioja Reserva** — $22. Manchego. "The Spanish board."

**WHAT TO SAY ON THE FLOOR**

"Hosting a wine and cheese night? I can pick 4 wines that pair perfectly — takes 2 minutes."

"Goat cheese on the menu? Sancerre. Literally the most famous pairing in wine."
