**THE STORY**

Rosé has the strangest reputation arc in American wine. In the 1980s-90s, White Zinfandel (Sutter Home, Beringer) made pink wine America's best-seller — and also made wine snobs cringe. When Provence-style dry rosé arrived in the 2000s (pale, bone-dry, mineral), it had to fight against the sweet-pink stigma.

It won. By 2018, rosé was the fastest-growing wine category in America. Instagram helped — the pale salmon color is photogenic. But the real driver was taste: dry rosé is genuinely one of the most versatile, food-friendly, refreshing wines you can drink.

**THE STYLES**

• **Provence (France)** — the template. Pale salmon, bone-dry, mineral, white peach, herbs. Whispering Angel, Miraval, Domaines Ott. $15-50.
• **California** — ranges from Provence-style (pale, dry) to fruit-forward (deeper pink). Sofia, Belle Glos, Lorenza.
• **Spanish** — deeper pink, strawberry, slightly more body. $10-18. MUGA Rosado.
• **White Zinfandel** — sweet, low-ABV, bright pink. Sutter Home, Beringer. Don't judge — it's someone's favorite.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Whispering Angel** — $22. The rosé that started the modern craze. Provence, pale, dry. "THE rosé. Everyone knows it. Pool, brunch, Tuesday."

2. **Miraval** — $22. Brad Pitt + Angelina Jolie's Provence estate. Celebrity cachet + legitimately great wine. "The one they'll Instagram."

3. **La Crema Monterey Rosé** — $18. California, fruit-forward but dry. Versatile. "Less expensive than Whispering Angel, slightly more fruit."

4. **MUGA Rosado** — $14. Spain (Rioja). Deeper pink, strawberry, food-friendly. "The value rosé with real character."

5. **Sutter Home White Zinfandel** — $6. Sweet, pink, huge volume seller. "Don't snob it. Smile, bag it, move on."

**WHAT TO SAY ON THE FLOOR**

"Dry rosé for the pool? Whispering Angel, $22. The standard."

"Rosé but on a budget? MUGA from Spain, $14. More body, more flavor, less money."

"Customer asks for White Zin: 'Great choice!' — no judgment. If they're curious about dry rosé: 'Next time, try Whispering Angel — completely different experience.'"

"ALWAYS sell current vintage rosé. Last year's rosé = faded, tired."
