**THE STORY**

Sonoma County lives in Napa's shadow — but many winemakers prefer it. While Napa is a single narrow valley, Sonoma is a patchwork of 18 different AVAs spanning from foggy coastline to warm inland valleys. This diversity means Sonoma can grow almost anything well — and its whites are where it really shines.

Fumé Blanc (Robert Mondavi's term for oaked Sauvignon Blanc) was born in Sonoma's Dry Creek Valley. The region's Sauvignon Blanc sits between the aggression of New Zealand and the restraint of Loire — tropical but balanced, textured but fresh.

**THE WHITES TO KNOW**

• **Sauvignon Blanc / Fumé Blanc** — Dry Creek Valley specialty. Grapefruit, melon, herbs, sometimes light oak. Ferrari-Carano Fumé Blanc, Honig, Duckhorn, Murphy-Goode.
• **Chardonnay** — from Sonoma Coast (cool, mineral, modern) to Russian River (richer, more fruit). Different from Napa Chard.
• **Viognier** — aromatic, honeysuckle, apricot. Dry but smells sweet. Small plantings, worth discovering.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Ferrari-Carano Fumé Blanc** — $14. THE California Fumé Blanc. Grapefruit, melon, touch of oak. "Summer white under $15 that tastes like $25. This moves."

2. **Honig Sauvignon Blanc** — $18. Napa/Sonoma. Clean, crisp, citrus-forward. "The step up from Ferrari-Carano."

3. **La Crema Sonoma Coast Chardonnay** — $22. Balanced, crowd-pleasing, light oak. "Thanksgiving wine. Everyone at the table agrees on La Crema."

4. **Duckhorn Sauvignon Blanc** — $25. Napa Valley technically, Sonoma-distributed. Rich, layered. "Premium Sauv Blanc that earns its price."

5. **Cline Viognier** — $12. Aromatic, honeysuckle, peach. "Under $12 and aromatic? This is the hidden gem."

**WHAT TO SAY ON THE FLOOR**

"Summer porch wine? Ferrari-Carano Fumé Blanc, $14. Done."

"Want something aromatic but dry? Viognier — smells sweet, tastes dry. It surprises everyone."
