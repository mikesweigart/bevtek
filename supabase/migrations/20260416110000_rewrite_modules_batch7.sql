**THE STORY**

The Loire Valley is France's garden — 600 miles of river winding through châteaux, goat farms, and some of the most food-friendly wines on Earth. While Bordeaux and Burgundy get the prestige, the Loire quietly produces wines that sommeliers drink on their nights off.

Sancerre, a hilltop medieval town overlooking the Loire river, has been growing Sauvignon Blanc since at least the 12th century. But the wine only became internationally famous in the 1970s-80s, when Paris bistros made it their house white. The pairing of Sancerre with the region's famous goat cheese (Crottin de Chavignol) is one of the greatest food-wine matches ever discovered — and it happened naturally, over centuries, because both grew in the same place.

**THE WINES**

• **Sancerre** — 100% Sauvignon Blanc. Crisp, mineral, citrus, gooseberry, chalky finish. More restrained than NZ Sauv Blanc. Classic goat-cheese wine. $25-50.
• **Pouilly-Fumé** — across the river from Sancerre. Also Sauvignon Blanc. Slightly smokier (fumé = smoky) due to flint soils. $25-50.
• **Vouvray** — 100% Chenin Blanc. Can be dry, off-dry, or sweet. Apple, honey, wet-wool note. Ages beautifully — 20+ years.
• **Chinon** — 100% Cabernet Franc red. Pencil shavings, red bell pepper, cranberry. Light, food-friendly.
• **Muscadet** — the coast. 100% Melon de Bourgogne. THE French oyster wine. Under $15.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Pascal Jolivet Sancerre** — $25. Clean, mineral, textbook Sancerre. "The everyday French Sauv Blanc."

2. **Domaine Vacheron Sancerre** — $35. Organic, biodynamic, more complexity. "The sommelier's Sancerre."

3. **Domaine Huet Vouvray** — $25-40. Legendary Chenin Blanc producer. Dry to sweet options. "If they've never tried Chenin Blanc, this converts them."

4. **Charles Joguet Chinon** — $22. Benchmark Loire Cab Franc. "Thanksgiving's secret weapon — pairs with turkey, cranberry, and stuffing all at once."

5. **Muscadet Sèvre-et-Maine** — $12-15. Any good producer. "Oysters? Under $15? This."

**WHAT TO SAY ON THE FLOOR**

"Goat cheese dinner? Sancerre — it's the pairing that's been working for 800 years."

"Thanksgiving wine that handles everything on the table? Chinon — Loire Cab Franc. Light, earthy, bridges turkey to cranberry to stuffing."

"Budget seafood wine? Muscadet under $15. The French drink it with every shellfish plate."
