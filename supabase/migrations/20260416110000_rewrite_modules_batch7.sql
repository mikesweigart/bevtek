**THE STORY**

Washington state is America's best-kept wine secret. It's the second-largest US wine producer (after California), but most Americans couldn't name a single Washington winery. That's changing fast — and the value proposition is extraordinary.

Eastern Washington is a desert (8 inches of rain annually), irrigated by the Columbia River. The climate is EXTREME: summer days hit 100°F, but nights drop to 50°F. That hot-cold swing is magic for grapes — it develops intense fruit flavor while preserving bright acidity. The growing season has 17+ hours of daylight in summer (2 hours more than Napa), which means more photosynthesis, more ripening potential.

The industry is young — most Washington wineries were founded after 1980. There's no century-old prestige pricing here. You get Napa-quality wine at $20-40 instead of $60-100.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Columbia Crest H3 Cabernet** — $12. Incredible value. Horse Heaven Hills. Dark fruit, smooth, structured. "Under $12 Cabernet that drinks like $25. The best value red in the store."

2. **Chateau Ste. Michelle Indian Wells Cabernet** — $18. Washington's largest winery, consistently excellent. "Napa quality at half the price. Not a secret — everyone in the industry knows."

3. **K Vintners Syrah** — $30-50. Charles Smith's project. Bold, concentrated, Walla Walla Syrah. "Washington's answer to Northern Rhône — pepper, smoke, dark fruit."

4. **Quilceda Creek Cabernet** — $150+. Washington's cult wine. Perfect scores from critics. "This is what Washington can do at the very top. Collector-level."

5. **L'Ecole No. 41 Merlot** — $25. Walla Walla. Plum, chocolate, herbs. "Merlot's reputation was ruined by Sideways. Washington never got the memo — their Merlot is world-class."

**WHAT TO SAY ON THE FLOOR**

"Love Napa Cab but not the price? Columbia Crest H3 — $12. Same quality grapes, different address."

"Want to impress a wine person? Tell them about Washington Syrah. K Vintners. They probably haven't tried it. They'll thank you."
