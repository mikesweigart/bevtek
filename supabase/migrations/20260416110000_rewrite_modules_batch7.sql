**THE STORY**

Australian Shiraz is one of the great success stories in wine. The same grape as French Syrah (brought to Australia in 1832 by James Busby, the "father of Australian wine"), but a completely different personality. Where Northern Rhône Syrah is peppery, elegant, and restrained, Australian Shiraz is bold, generous, fruit-forward, and often 14-15% alcohol.

Australia's wine boom happened in the 1980s-90s when brands like Yellow Tail, Jacob's Creek, and Lindeman's flooded the world with affordable, friendly Shiraz. The backlash came in the 2000s — critics called it "fruit bombs" and "over-oaked." But the pendulum has swung: today's best Australian Shiraz balances power with elegance, and the country's cool-climate regions (Yarra Valley, Adelaide Hills, Eden Valley) produce Syrah that rivals the Northern Rhône.

**THE REGIONS**

• **Barossa Valley** — the benchmark. Warm, rich, plum, blackberry, chocolate, leather. Penfolds Grange territory. BIG wines.
• **McLaren Vale** — coastal, slightly fresher. Dark fruit + savory. D'Arenberg, Mollydooker.
• **Clare / Eden Valley** — cooler, peppery, more like Northern Rhône. Henschke Hill of Grace country.
• **Hunter Valley** — elegant, lower-alcohol, earthy. Tyrrell's, Brokenwood.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Penfolds Bin 389** — $40. "Baby Grange." Cabernet-Shiraz blend aged in ex-Grange barrels. "The affordable path into Australia's greatest wine."

2. **Two Hands Gnarly Dudes** — $20. Barossa. Dark fruit, chocolate, spice. "Under $20 Barossa Shiraz that drinks like $35."

3. **d'Arenberg The Dead Arm** — $35. McLaren Vale. Dense, dark, complex. "The wine-geek's Australian Shiraz."

4. **Yellow Tail Shiraz** — $7. The #1 Australian wine in America. Simple, fruity, entry-level. "Not a masterpiece — but it introduced a generation to Shiraz."

5. **Torbreck RunRig** — $150+. Barossa's cult Shiraz. Massive, old-vine, extraordinary. "Australia's Penfolds Grange alternative."

**WHAT TO SAY ON THE FLOOR**

"Want a BIG red? Barossa Shiraz. Two Hands Gnarly Dudes at $20 is the value play."

"Loves Napa Cab? Australian Shiraz is the Southern Hemisphere equivalent — same boldness, different spice profile."

"BBQ wine? Shiraz was MADE for grilled meat."
