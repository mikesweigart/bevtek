**THE STORY**

Rye whiskey is America's original cocktail spirit. Before bourbon dominated, rye was king — George Washington distilled it at Mount Vernon, and it was the backbone of Philadelphia and Maryland's distilling culture. The Manhattan, the Old Fashioned, the Sazerac — all were invented with rye, not bourbon.

Prohibition nearly killed rye. When it ended in 1933, bourbon's sweeter, easier profile won American tastes. Rye almost disappeared entirely. By the early 2000s, you could count the available rye whiskeys on one hand.

Then the craft cocktail revival happened. Bartenders rediscovered classic pre-Prohibition recipes — and they all called for rye. Demand surged. Today rye is the fastest-growing American whiskey category, and every serious bar stocks at least three.

**RYE vs. BOURBON — THE 30-SECOND EXPLANATION**

Same rules, different grain. Bourbon = 51%+ corn (sweet, vanilla). Rye = 51%+ rye grain (spicy, peppery, dry). Think of it like the difference between cornbread (bourbon) and sourdough (rye) — same method, completely different personality.

Rye is DRIER, SPICIER, and MORE ASSERTIVE than bourbon. In cocktails, rye cuts through vermouth and bitters where bourbon can get lost. That's why bartenders prefer it.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Rittenhouse Bottled-in-Bond** — $28. THE bartender's rye. 100 proof, bold, spicy, stands up in any cocktail. 51% rye (barely legal), which means it's on the softer end for rye. "Every serious home bar needs this bottle."

2. **Bulleit Rye** — $30. 95% rye (sourced from MGP Indiana). Big spice, peppery, clean. Fantastic in Manhattans. "If they want SPICE, this delivers."

3. **WhistlePig 10 Year** — $75. Vermont-finished Canadian rye. 100% rye, 100 proof, 10 years old. Silky, complex, sippable. "The gift-worthy rye. Whiskey lovers geek out over this."

4. **Sazerac Rye** — $35. From Buffalo Trace. Smooth, subtle, New Orleans heritage. "The Sazerac cocktail is named after this whiskey. Pair them."

5. **High West Double Rye** — $38. Blend of young (2-year, spicy) and old (16-year, mellow) rye. Mint, clove, cinnamon. "The best of both worlds — young energy, old wisdom."

**WHAT TO SAY ON THE FLOOR**

"Making Manhattans? You want rye, not bourbon. Rittenhouse at $28 — every bartender in the country uses it."

"Bourbon drinker who wants to branch out? Rye is the natural next step. Same family, different personality — drier, spicier."

"Gift for a whiskey nerd? WhistlePig 10 Year — $75. They'll know you know."
