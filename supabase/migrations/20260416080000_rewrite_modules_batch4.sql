**THE STORY**

Oregon's wine industry was built on a dare. In the 1960s, UC Davis (California's wine school) told David Lett — a young enology student — that Pinot Noir could NOT be grown commercially in the Pacific Northwest. The climate was too cool, the region too remote, the industry nonexistent.

Lett ignored them. In 1966, he planted Pinot Noir vines in the Willamette Valley — and named his winery "The Eyrie Vineyards" (after the hawks nesting in the hills). Other idealists followed: Dick Erath, Dick Ponzi, Susan Sokol Blosser.

The world noticed in 1979, when Eyrie Vineyards' 1975 Pinot Noir placed in the top ten at a major French tasting — beating some Burgundies. Robert Drouhin (one of Burgundy's most famous producers) was so impressed that he bought land in Oregon and opened Domaine Drouhin — Burgundy's first outpost in America.

Today, the Willamette Valley is America's most respected Pinot Noir region, and Oregon produces some of the finest Pinot outside of Burgundy itself.

**WHY OREGON, NOT CALIFORNIA?**

Pinot Noir is a finicky grape — it hates heat. California's warm valleys produce riper, plummier Pinot. Oregon's cooler, rainier climate gives Pinot what it craves: slow ripening, preserved acidity, earthy complexity. Oregon Pinot tastes like red cherry, cranberry, forest floor, and spice. California Pinot tastes like plum, cola, and vanilla.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **A to Z Wineworks** — $18. The Oregon value benchmark. Every vintage is reliable, every bottle drinks above its price. "Under $20 Oregon Pinot? This is the answer. Every time."

2. **Erath Oregon** — $18. One of the founding wineries. Fruit-forward, approachable, widely distributed. "Erath is how most people discover Oregon Pinot."

3. **Domaine Drouhin Oregon** — $45. The Burgundy-meets-Oregon icon. Elegant, silky, mushroom and red fruit. "Tell the customer the Drouhin story — a Burgundy family so impressed by Oregon they moved here."

4. **Ponzi Tavola** — $25. One of the pioneering families. Balanced, earthy, true Willamette character. "Oregon loyalty in a bottle."

5. **Beaux Frères** — $60. Co-founded by Robert Parker's brother-in-law. Dense, complex, Ribbon Ridge. "The wine-collector's Oregon Pinot."

**WHAT TO SAY ON THE FLOOR**

"Salmon tonight? Oregon Pinot Noir. It's the state fish + the state wine. They were made for each other."

"Likes Burgundy but can't afford it? Domaine Drouhin Oregon — $45 vs. $80+ for comparable Burgundy."

"Never tried Oregon? A to Z at $18. Zero risk, genuine quality."
