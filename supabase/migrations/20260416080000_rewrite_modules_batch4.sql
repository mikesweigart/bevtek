**THE STORY**

Blended Scotch is the most consumed whisky on Earth — 90% of all Scotch sold worldwide is blended. It was invented in the 1860s by Edinburgh grocer Andrew Usher, who realized that combining malt whiskies from different distilleries with lighter grain whisky produced a more consistent, accessible spirit.

The great blending houses — Johnnie Walker, Dewar's, Chivas Regal, Ballantine's — built the global Scotch industry. Single malts were a niche curiosity until the 1980s. Even today, for every bottle of Glenfiddich sold, 10 bottles of Johnnie Walker are poured.

**THE MISUNDERSTANDING**

"Blended = inferior" is the single most persistent myth in the whisky world. It's wrong. Blending is a skill. Master blenders (like Johnnie Walker's Jim Beveridge or Compass Box's John Glaser) create complexity by combining 20-40 individual whiskies. A great blend harmonizes — each component adds something the others lack.

**HOW BLENDING WORKS**

A blended Scotch combines:
• **Malt whisky** (from single malt distilleries — Speyside, Highland, Islay, etc.) — provides flavor, character, complexity
• **Grain whisky** (from industrial column stills) — provides smoothness, body, volume

The higher the malt percentage, generally the more complex (and expensive) the blend.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Johnnie Walker Black Label** — $33. 12-year blend. Smoky, smooth, balanced. The global benchmark. "Don't dismiss it because it's everywhere. Black Label is a genuinely great 12-year-old whisky. Period."

2. **Monkey Shoulder** — $30. Blend of three Speyside single malts (Glenfiddich, Balvenie, Kininvie). Smooth, vanilla, banana bread. "The cocktail Scotch. Makes the best Penicillin you've ever had."

3. **Chivas Regal 12** — $30. Speyside-heavy, round, sweet, approachable. Huge in Asia and Europe. "The Scotch for people who want smoothness above all."

4. **Johnnie Walker Blue Label** — $200. The prestige blend. Ultra-smooth, honeyed, rare malts including now-closed distilleries. "You're paying for the experience and the bottle. It IS special — just know what you're buying."

5. **Compass Box Peat Monster** — $55. Craft blend focused entirely on peat. Smoky, complex, modern. "Gift for a peated Scotch lover who's tried everything from Islay."

**WHAT TO SAY ON THE FLOOR**

"Customer says 'I hate Scotch' → 99% of the time they had a cheap blend served badly. Offer Monkey Shoulder or JW Black with one ice cube. Different experience entirely."

"Is blended worse than single malt? No — it's different. Single malt is one voice. A blend is a choir. Both can be beautiful."

"Gift under $35? Johnnie Walker Black. Everyone knows the name, and the quality justifies it."
