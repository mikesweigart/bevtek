**THE STORY**

Belgium is a country of 11 million people and over 300 breweries — more per capita than anywhere on Earth. Belgian brewing tradition stretches back to the Middle Ages, when monasteries brewed beer as a source of nutrition and income. Six Trappist monasteries STILL brew commercially today, following rules set by monks centuries ago.

What makes Belgian beer unique isn't hops or malt — it's YEAST. Belgian yeast strains produce flavors no other brewing tradition creates: banana, pear, bubblegum, clove, black pepper, cotton candy. At warm fermentation temperatures, these yeasts go wild with esters and phenols.

**THE STYLES YOUR STAFF MUST KNOW**

• **Witbier (Belgian White)** — unfiltered wheat beer with coriander + orange peel. Light, citrusy, 4.5-5.5% ABV. Hoegaarden invented it; Allagash White perfected it for Americans. "The gateway Belgian."

• **Dubbel** — dark, malty, fig + raisin + caramel. 6-8% ABV. Chimay Red, Westmalle Dubbel. "Like liquid bread with dark fruit."

• **Tripel** — golden, STRONG (7.5-9.5%), deceptively smooth. Banana, spice, honey. Westmalle Tripel, Chimay White. "This will sneak up on you — 9% that drinks like 5%."

• **Quadrupel / Belgian Strong Dark** — massive. Dried fruit, chocolate, molasses. 9-12% ABV. Rochefort 10, St. Bernardus Abt 12. "The nightcap. One glass. Savor it."

• **Saison** — dry, peppery, effervescent farmhouse ale. 5-8% ABV. Saison Dupont is THE classic. "The food beer — it pairs with everything."

• **Lambic / Gueuze** — spontaneously fermented (wild yeast). Sour, funky, cidery. Cantillon, Drie Fonteinen. "The Champagne of beer."

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Allagash White** — $12/4-pack. Maine brewery, Belgian style. The best American Witbier. "Customer drinks Blue Moon? Hand them Allagash White. They'll never go back."

2. **Chimay Blue (Grand Réserve)** — $12/750ml. Trappist Quadrupel, 9% ABV. Dark fruit, chocolate, complex. "Made by actual monks. That's not marketing — it's real."

3. **Delirium Tremens** — $15/4-pack. Strong golden ale, 8.5%. Pink elephant on the label. Fruity, peppery, iconic. "The party Belgian — everyone remembers the pink elephant."

4. **Saison Dupont** — $10/750ml. The definitive farmhouse ale. Dry, peppery, effervescent. "If you could only drink one Belgian beer for the rest of your life, most brewers would pick this."

5. **Westmalle Tripel** — $12/750ml. The original Tripel (Westmalle invented the style in 1934). Banana, honey, 9.5% ABV. "The blueprint that every Tripel in the world copies."

**WHAT TO SAY ON THE FLOOR**

"Likes Blue Moon? Allagash White — same vibe, genuine Belgian style."

"Wants something strong but not hoppy? Belgian Tripel — 9% but smooth and honeyed."

"Special occasion beer? Chimay Blue or Rochefort 10. Open with reverence."
