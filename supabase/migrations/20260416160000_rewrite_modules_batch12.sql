**THE STORY**

Italian white wine is the world's go-to for light, food-friendly sipping. Pinot Grigio alone accounts for more white wine imports to the US than any other single varietal. But Italy's white wine scene extends far beyond PG — and staff who know the alternatives become the trusted experts customers seek out.

**THE WHITES**

• **Pinot Grigio** — Italy's #1 white export. Light, crisp, neutral to citrusy. Ranges from watery (cheap Veneto) to genuinely excellent (Alto Adige). $10-25.

• **Vermentino** — Sardinia and Liguria's coastal white. Crisp, saline, almond, citrus. THE Mediterranean seafood wine. Underpriced and underrated. $12-20.

• **Soave** — Garganega grape from Veneto. Almond, white flowers, mineral. The best alternative to Pinot Grigio. $12-18.

• **Gavi** — Cortese grape from Piedmont. Lemon, green apple, bone-dry. Elegant aperitif wine. $15-22.

• **Falanghina / Fiano / Greco** — Campania's whites (southern Italy). Richer, more textured, sometimes waxy. "The new frontier" of Italian whites.

• **Arneis** — Piedmont. Pear, herbs, almond. Lovely alternative to everything above.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Santa Margherita Pinot Grigio** — $22. THE premium PG that created the category in America. "The name everyone knows."

2. **Argiolas Vermentino di Sardegna** — $14. Sardinian, saline, crisp. "The oyster wine from the Mediterranean."

3. **Pieropan Soave Classico** — $16. Benchmark Soave. Almond, white flowers, clean. "Better than most Pinot Grigio at the same price."

4. **Livio Felluga Pinot Grigio** — $22. Alto Adige quality. Structure, acid, mineral. "Premium PG that earns its price."

5. **Feudi di San Gregorio Falanghina** — $14. Campania. Rich, textured, honeyed edge. "The southern Italian discovery."

**WHAT TO SAY ON THE FLOOR**

"Like Pinot Grigio but want MORE? Vermentino or Soave — same vibe, more character."

"Oyster/shellfish dinner? Vermentino. It's what they drink on the Italian coast."

"Gift: Santa Margherita — everyone recognizes the name."
