**THE MATH THAT CHANGES EVERYTHING**

A customer who visits once a month at $30/visit = $360/year. A wine club member who commits to $60/month = $720/year. A loyal customer who spends $100+/month and sends friends = $1,200+/year.

Loyalty programs don't just increase spend — they change BEHAVIOR. A punch card makes someone choose your store over the competitor three blocks away. A monthly pick makes them discover wines they'd never try. A text alert for allocated bottles makes them feel like an insider.

**WHAT WORKS FOR SMALL LIQUOR STORES**

**1. Punch Card (cost: $0)**
Buy 12 bottles, get 1 free. Simple. No technology. Laminate 50 cards and put them at the register. The psychology: "I only need 3 more punches" keeps them coming back.

**2. Monthly "Staff Picks" Email (cost: $0)**
3-5 wines your staff genuinely recommends each month, with one-line tasting notes and prices. Send via Mailchimp free tier or even a group text. Builds authority. Drives traffic.

**3. First-Access Text Alerts (cost: $0)**
When you receive allocated bottles (Pappy, Blanton's, rare Scotch), text your best customers FIRST. "Just received 4 bottles of Eagle Rare — first come, first served." They'll run.

**4. Monthly Subscription Box ($50-100/month)**
Curate 2-3 bottles at a set price. Staff selects wines with tasting notes. Members get first access to new arrivals and invitations to tastings. Higher commitment, higher retention.

**5. Tasting Events ($20-30 cost per event)**
Monthly in-store tasting: 4-5 wines, 1 oz pours, Thursday or Friday evening. Cost: $20-30 in opened bottles. Revenue generated: $200-500 in immediate sales plus long-term loyalty. Partner with local restaurants or cheese shops.

**THE DATA PLAY (with BevTek)**

Megan tracks what each staff member asks about, what customers search for in the Assistant, and what training modules are completed. Over time, this data powers personalized recommendations: "Hey Mike, we just got that Barolo you asked about last month." THAT is the loyalty killer app.

