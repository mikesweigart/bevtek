**THE STORY**

Italy has more indigenous grape varieties than any country on Earth — over 500. But three red grapes dominate exports and should dominate your knowledge: Sangiovese (Tuscany), Nebbiolo (Piedmont), and Corvina (Veneto).

Italy's wine laws divide quality into tiers: IGT (basic), DOC (controlled origin), and DOCG (controlled and guaranteed — the top). Unlike France, Italian regions are constantly evolving their rules — which makes Italy both exciting and confusing.

**SANGIOVESE — TUSCANY'S GRAPE**

• **Chianti Classico** — Sangiovese-based (80%+). Cherry, leather, herbs, bright acid. THE pasta wine. $15-30. Antinori, Felsina, Castellare.
• **Brunello di Montalcino** — 100% Sangiovese, aged 5+ years before release. Powerful, complex, age-worthy. $50-200. Casanova di Neri, Il Poggione, Biondi-Santi.
• **Super Tuscans** — Cabernet/Merlot blended with Sangiovese, outside traditional rules. Sassicaia ($200+), Tignanello ($100), Ornellaia ($200+).

**CORVINA — VENETO'S GRAPE**

• **Valpolicella** — light, cherry, everyday. $12-18.
• **Ripasso** — Valpolicella refermented on dried Amarone grape skins. Richer, "baby Amarone." $18-30. The VALUE play.
• **Amarone della Valpolicella** — dried-grape method (appassimento). MASSIVE. 15%+ ABV. Dried cherry, chocolate, fig. $40-120. Special occasion wine.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Antinori Chianti Classico Riserva** — $25. The Antinori family has been making wine since 1385. "625 years of winemaking in this bottle."

2. **Masi Costasera Amarone** — $50. Entry-level Amarone that delivers. "Like drinking raisins soaked in chocolate and coffee."

3. **Allegrini Palazzo della Torre** — $18. Ripasso method. Rich, fruit-forward, crowd-pleasing. "The $18 wine that tastes like $35 Amarone."

4. **Banfi Brunello di Montalcino** — $60. Accessible, well-distributed Brunello. "Tuscany's greatest grape at its most concentrated."

5. **Montepulciano d'Abruzzo** (not from Montepulciano the town) — $10-14. Soft, fruity, zero pretension. "Tuesday pasta wine under $14."

**WHAT TO SAY ON THE FLOOR**

"Tuesday pasta night? Chianti Classico — born for tomato sauce."

"Special Italian dinner? Amarone. Rich, powerful, unforgettable."

"Value Italian red? Allegrini Palazzo della Torre — $18, drinks like $35."
