**YOUR SAFETY COMES FIRST — ALWAYS**

Liquor stores are high-value retail targets. Bottles are expensive, portable, and easily resold. Staff need to understand both the prevention side (stopping theft before it happens) and the response side (what to do when it does).

**THEFT PREVENTION — THE DAILY HABITS**

1. **Greet every customer who walks in.** Eye contact + "Welcome!" — this is the #1 deterrent, proven by loss prevention research. Thieves do NOT want to be remembered or acknowledged.

2. **Keep the store well-lit.** Dark corners invite concealment. If you can't see a section clearly, neither can your cameras.

3. **High-value items behind the counter or in locked cases.** Pappy, Blue Label, allocated bourbon, anything over $100 — don't leave it on open shelves.

4. **Cameras visible.** Signs: "Smile, you're on camera." The deterrent value of VISIBLE cameras is greater than hidden ones.

5. **Keep exits clear and visible.** Staff should be able to see who enters and leaves.

**IF YOU SEE SHOPLIFTING**

• **DO NOT physically confront.** Note their description (height, clothing, direction they went).
• Tell your manager immediately.
• If the person is still in the store, your manager decides whether to approach.
• If they've left, report to police with the description and any camera footage.
• Your safety > any bottle, no matter how expensive.

**ROBBERY PROTOCOL**

This is the protocol you hope to never use:
1. **Comply.** Give them whatever they want. Money, bottles, whatever.
2. **Don't be a hero.** Don't chase. Don't argue. Don't resist.
3. **After they leave:** lock the door, call 911, preserve the scene (don't touch what they touched).
4. **Write down everything you remember** while it's fresh — description, direction, weapon, words.
5. **Trauma is normal.** Talk to your manager about support resources. This is a real event with real emotional impact.

**CASH HANDLING**

• Regular drops to the safe — never let the register exceed $200-300
• Two people for end-of-day close and counting
• Count back change to customers (builds trust, reduces disputes)
• Never discuss cash amounts in front of customers

**SLIP AND FALL**

• Clean spills IMMEDIATELY — broken bottles, cooler condensation, rain tracking
• "Wet floor" signs, always
• Heavy items on lower shelves in the stockroom
• Know where your fire exits are

**THE BOTTOM LINE**

Be observant. Follow protocol. Go home safe. Everything else is replaceable.
