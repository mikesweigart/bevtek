**THE STORY**

Cabernet Sauvignon is the king of red grapes — planted in every wine-producing country on Earth, collected by every serious wine drinker, and paired with every steak dinner in America. It's a natural cross between Cabernet Franc and Sauvignon Blanc, first identified in the 1700s in Bordeaux.

What makes Cab special is its STRUCTURE. Small berries, thick skins, deep color, firm tannins — wines that can age 20+ years and actually improve. This aging potential is what drives the collector market and the prestige pricing.

**FOUR REGIONS, FOUR PERSONALITIES**

**Bordeaux Left Bank** — blended (65-85% Cab with Merlot, Cab Franc). Cedar, tobacco, dark fruit. Austere young, magnificent aged. $30-$500+.

**Napa Valley** — bolder, riper. Blackcurrant, chocolate, vanilla. Often 100% Cab. $30-300. The American icon.

**Chile (Maipo, Colchagua)** — the VALUE king. Ripe dark fruit, eucalyptus, mint. $10-25 for quality rivaling $40+ elsewhere.

**Australia (Coonawarra, Margaret River)** — minty, eucalyptus, structured. Terra rossa soils. $20-60.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Concha y Toro Casillero del Diablo** — $10. Chilean. "The best value Cab in the store."
2. **Louis Martini Sonoma** — $18. California. Reliable, smooth.
3. **Château Meyney Saint-Estèphe** — $30. Bordeaux. "Real Bordeaux at $30."
4. **Silver Oak Alexander Valley** — $65. The steakhouse icon.
5. **Caymus Napa** — $85. Cult-everyday Napa Cab.

**WHAT TO SAY ON THE FLOOR**

"Steak? Cabernet Sauvignon. Every time. Start at $10 (Chile) or $65 (Napa) — there's a Cab for every budget."

"Value Cab? Chile. Casillero del Diablo at $10 rivals $25+ bottles from other countries."
