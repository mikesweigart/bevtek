**THE STORY**

Rosé has the strangest reputation arc in American wine. For decades, "pink wine" meant White Zinfandel — sweet, cheap, and dismissed by wine people. Then Provence-style dry rosé arrived in the 2000s: pale salmon, bone-dry, mineral, sophisticated. It had to fight against the sweet-pink stigma.

By 2018, dry rosé was the fastest-growing wine category in America. Whispering Angel became a cultural phenomenon. "Rosé all day" became a lifestyle brand. The French were thrilled — they'd been drinking dry rosé year-round for generations. Americans finally caught on.

**HOW ROSÉ IS MADE**

Red grapes + short skin contact (2-20 hours). The shorter the maceration, the paler the wine. Rosé is NOT made by blending red and white wine (with the exception of rosé Champagne). The color comes entirely from brief contact with red grape skins.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Whispering Angel (Château d'Esclans)** — $22. THE rosé. Provence. Pale, dry, mineral. "The one everyone asks for by name."
2. **Miraval** — $22. Brad Pitt's Provence estate. Celebrity + quality. "The Instagram rosé that's actually great."
3. **La Crema Monterey Rosé** — $18. California. Fruit-forward, dry. "Less than Whispering Angel, slightly more fruit."
4. **MUGA Rosado** — $14. Spain (Rioja). Deeper pink, strawberry, food-friendly. "The value rosé with real character."
5. **Sutter Home White Zinfandel** — $6. Sweet, pink, enormous volume. "Don't judge. Smile, bag, move on."

**WHAT TO SAY ON THE FLOOR**

"Dry rosé? Whispering Angel, $22. Standard. Or MUGA at $14 for value."

"Customer asks for White Zin: 'Great choice!' If they're curious: 'Next time, try Whispering Angel — totally different experience.'"

"ALWAYS sell current vintage. Last year's rosé = faded."
