**THE STORY**

Sauvignon Blanc is the most versatile white grape in the world — it produces radically different wines depending on where it grows. Loire Valley Sancerre tastes like wet rocks and chalk. New Zealand Marlborough tastes like a passionfruit explosion. California Fumé Blanc tastes like ripe melon with a whisper of oak. Same grape, different planet.

The grape originated in France's Loire Valley, where it's been grown for centuries. The name likely comes from "sauvage" (wild) — a reference to its vigorous, untamed vines. In the 1980s, New Zealand's Marlborough region created an entirely new style — explosive tropical fruit — that took the world by storm.

**FIVE STYLES, ONE GRAPE**

• **Loire (Sancerre, Pouilly-Fumé)** — mineral, flinty, restrained. Classic oyster wine. $25-50.
• **New Zealand (Marlborough)** — passionfruit, grapefruit, cut grass. The most distinctive. $14-30.
• **California** — between Loire and NZ. Sometimes oaked ("Fumé Blanc"). $15-25.
• **South Africa** — tropical with green edge. Excellent value. $10-18.
• **Chile** — crisp, green, herbal. Very affordable. $8-14.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Kim Crawford Marlborough** — $14. The #1 NZ Sauv Blanc in America. "The default."
2. **Cloudy Bay** — $25. The NZ Sauv Blanc that started everything.
3. **Pascal Jolivet Sancerre** — $25. French. Mineral, elegant.
4. **Ferrari-Carano Fumé Blanc** — $14. California oaked style. "Summer white under $15."
5. **Oyster Bay** — $12. NZ. Reliable, affordable, crowd-pleasing.

**WHAT TO SAY ON THE FLOOR**

"Goat cheese? Sancerre. Always."

"Summer sipper? Kim Crawford or Oyster Bay — NZ Sauv Blanc, $12-14."

"Likes NZ style but wants French? Sancerre — same grape, more restraint."
