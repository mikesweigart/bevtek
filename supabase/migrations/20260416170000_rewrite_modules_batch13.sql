**THE STORY**

In 2004, the movie Sideways nearly destroyed Merlot. Paul Giamatti's character declares "I am NOT drinking Merlot!" — and real-world sales dropped 2% overnight. Wine snobs piled on. For a decade, ordering Merlot was embarrassing.

This was deeply unfair. The greatest Merlots on Earth — Château Pétrus ($3,000+), Château Le Pin ($2,000+), Masseto ($500+) — are 100% Merlot and among the most celebrated wines ever made. What Sideways actually attacked was cheap, mass-market Merlot that flooded the US in the 90s. The quality stuff never wavered.

Today, Merlot is recovering. Washington state, Bordeaux's Right Bank, and quality-focused California producers are proving that Merlot — when done right — is supple, plummy, chocolatey, and genuinely delicious.

**WHY MERLOT IS GREAT**

• Softer tannins than Cabernet — drinks well younger
• Plum, cherry, chocolate, herbs — crowd-pleasing flavors
• Exceptional food wine — works with more dishes than Cab
• Better value — the Sideways stigma means prices stayed low

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Duckhorn Napa Merlot** — $55. THE American benchmark since 1978. "The Merlot that survived Sideways."
2. **Columbia Crest H3 Merlot** — $12. Washington. "Under $12 Merlot that's actually good."
3. **Château Canon Saint-Émilion** — $70. Right Bank Bordeaux. "World-class Merlot-dominant blend."
4. **Blackbird Vineyards Arise** — $30. Napa. Right Bank-inspired. Elegant.
5. **14 Hands Merlot** — $10. Washington. Everyday, simple, reliable.

**WHAT TO SAY ON THE FLOOR**

"Thinks they don't like Merlot? Hand them Duckhorn. They'll reconsider."

"Softer than Cab, more interesting than Pinot: that's Merlot's lane."

"Pasta with meat sauce: Merlot. The softness complements tomato without fighting it."
