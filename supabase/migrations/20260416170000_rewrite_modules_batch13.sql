**THE STORY**

Pinot Noir is wine's most temperamental, most transparent, and most rewarding grape. Unlike Cabernet (which makes good wine almost everywhere), Pinot Noir only excels in SPECIFIC climates — cool enough to preserve acidity and develop complexity, warm enough to ripen fully. Where it succeeds, it produces some of the most profound wines on Earth.

The name comes from "pine" (the grape clusters look like pine cones) and "noir" (black). It's thin-skinned, disease-prone, and notoriously difficult to grow. Winemakers call it "the heartbreak grape" because it magnifies every mistake. But it also magnifies terroir — the specific place — more than any other variety.

**FOUR REGIONS, FOUR PERSONALITIES**

**Burgundy (France)** — the gold standard. Earth, mushroom, dried cherry, truffle. High acid, ethereal. Expensive. $30-$500+. "Drinking Burgundy is like reading poetry."

**Oregon (Willamette Valley)** — Burgundy's American soulmate. Red cherry, cranberry, forest floor. Elegant, food-friendly. $18-80. "The closest American Pinot gets to Burgundy."

**New Zealand (Central Otago, Martinborough)** — bright cherry, thyme, wild herbs. Cool-climate intensity with Southern Hemisphere fruit. $20-50. "Stunning value."

**California (Sonoma Coast, Santa Rita Hills)** — riper, plummier, sometimes oaky. Plum, cola, vanilla. $20-100. "The fruity, accessible style."

**THE SHORTCUT**

Ask: "Do you prefer earthy or fruity?"
• **Earthy** → Burgundy or Oregon
• **Fruity** → California or New Zealand

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **A to Z Wineworks Oregon** — $18. The Oregon value champion.
2. **Meiomi** — $22. California. Rich, smooth, mega-popular. "The gateway Pinot."
3. **Louis Jadot Bourgogne Rouge** — $18. Real Burgundy, accessible price.
4. **Kim Crawford Pinot Noir** — $16. New Zealand. Bright, cherry, clean.
5. **Belle Glos Clark & Telephone** — $45. California. Dark, concentrated, special.

**WHAT TO SAY ON THE FLOOR**

"Salmon tonight? Pinot Noir — Oregon especially. They were made for each other."

"Never had Burgundy? Louis Jadot Bourgogne at $18. Real Burgundy, no intimidation."
