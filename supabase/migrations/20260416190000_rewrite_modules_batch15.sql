**BEYOND CHARDONNAY**

California's white wine scene extends far beyond Chard and Sauv Blanc — but most customers have never been introduced to the alternatives.

**VIOGNIER** — aromatic, honeysuckle, apricot, peach. Full body but dry. "It smells sweet but tastes dry — surprises everyone."

**PINOT GRIS** — pear, apple, light honey. More body than Italian Pinot Grigio. J Vineyards, La Crema.

**ALBARIÑO** — the Spanish coastal grape, now thriving in California. Crisp, saline, citrus. Tangent, Bokisch. "The seafood wine."

**RHÔNE WHITES (Grenache Blanc, Roussanne, Marsanne)** — rich, textured, food-friendly. Tablas Creek, Bonny Doon.

**TOP SELLERS**

