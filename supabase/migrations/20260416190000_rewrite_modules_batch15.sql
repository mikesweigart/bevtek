**THE STORY**

California grows virtually every red grape on Earth — but four dominate: Cabernet Sauvignon (Napa's king), Zinfandel (the heritage grape), Pinot Noir (Sonoma Coast, Santa Rita Hills), and Syrah/Petite Sirah (Paso Robles, Central Coast).

Beyond Napa Cab, California's red wine landscape is vast and exciting — and often much more affordable.

**ZINFANDEL** — California's "own" grape. Jammy, spicy, high-alcohol (often 15%+). Best from Dry Creek Valley and Lodi. Ridge Geyserville ($40), Turley ($40), Seghesio ($25). "BBQ wine. Period."

**SYRAH** — peppery, dark, meaty. Thrives in Paso Robles and Santa Barbara. Alban, Stolpman, Tensley. "The Rhône transplant."

**PETITE SIRAH** — not Syrah. Inky, tannic, dark chocolate. The grape that stains everything. "For when they want DARK."

**TOP SELLERS**

1. **Ridge Geyserville** — $40. "The greatest Zinfandel blend in America."
2. **Seghesio Old Vine Zin** — $25. "Sonoma Zin heritage at a fair price."
3. **Tablas Creek Esprit de Tablas** — $55. "Paso Robles Rhône blend perfection."
4. **Michael David 7 Deadly Zins** — $15. "Value Zin under $15."
5. **Meiomi Pinot Noir** — $22. "The most popular Pinot in America."

**WHAT TO SAY:** "BBQ? Zinfandel. Steak? Cabernet. Something different? Paso Robles Syrah."
