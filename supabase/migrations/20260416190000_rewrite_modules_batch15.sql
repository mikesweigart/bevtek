**THE STORY**

New Zealand Sauvignon Blanc didn't exist commercially until 1985, when Cloudy Bay released its first vintage from Marlborough. Within a decade, this tiny wine region at the bottom of the South Island had created an entirely new flavor profile — explosive passionfruit, grapefruit, gooseberry, cut grass — that no other region could replicate.

The climate is key: Marlborough's long, cool growing season preserves intense fruit aromatics while maintaining razor-sharp acidity. The result is wine that hits you in the face with flavor — which is exactly why it became the world's most recognizable Sauvignon Blanc style.

**TOP SELLERS**

1. **Kim Crawford** — $14. The #1 NZ Sauv Blanc in America. "The default recommendation."
2. **Cloudy Bay** — $25. The original. "The bottle that started everything."
3. **Oyster Bay** — $12. Reliable, affordable. "Under $12 and distinctive."
4. **Dog Point** — $20. Made by Cloudy Bay's original winemaker. "The upgrade."
5. **Greywacke** — $25. Made by Cloudy Bay's founder Kevin Judd. "The connoisseur's pick."

**WHAT TO SAY:** "Summer sipper? Kim Crawford, $14. Done. Want the story? Cloudy Bay — the one that started the revolution."
