**THE STORY**

Oregon is famous for Pinot Noir — but its whites are exceptional and underpriced. The Willamette Valley's cool climate produces whites with the kind of natural acidity and elegance that hot-climate regions struggle to achieve.

**PINOT GRIS** — Oregon's most-planted white. Richer than Italian Pinot Grigio, with pear, apple, and honeyed texture. King Estate, Ponzi, Chehalem. $15-25.

**CHARDONNAY** — lean, mineral, Burgundian. Oregon Chard is to California Chard what Oregon Pinot is to California Pinot — more restraint, more acid, more terroir. Roco, Domaine Drouhin, Stoller. $25-45.

**RIESLING** — small but excellent. Dry to off-dry from cooler Willamette sites. Trisaetum, Brooks. $18-28.

**TOP SELLERS**

1. **King Estate Pinot Gris** — $16. "Oregon's signature white. The gateway."
2. **Domaine Drouhin Chardonnay** — $30. "Burgundy quality, Oregon address."
3. **A to Z Pinot Gris** — $14. "The value leader."
4. **Ponzi Pinot Gris** — $18. "Pioneer family, pioneer wine."
5. **Trisaetum Riesling** — $22. "Oregon Riesling? Yes, and it's gorgeous."

**WHAT TO SAY:** "Likes Italian PG? Oregon Pinot Gris is the upgrade — same grape, more character."
