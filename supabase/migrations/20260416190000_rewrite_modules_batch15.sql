**THE STORY**

South Africa has been making wine for 350+ years — since Jan van Riebeeck planted the first vines at the Cape in 1659. But apartheid-era sanctions kept South African wine off the world stage for decades. Since 1994, the industry has exploded — and the value proposition is extraordinary.

Two grapes define the country: **Chenin Blanc** (their signature white) and **Pinotage** (a unique cross of Pinot Noir and Cinsaut, created in SA in 1925). But Stellenbosch also produces world-class Cabernet and Bordeaux-style blends at prices that shame Napa.

**TOP SELLERS**

1. **Mullineux Chenin Blanc** — $25. "The benchmark. Best-value white wine in the world."
2. **Kanonkop Pinotage** — $30. "THE Pinotage. If you only try one, this."
3. **Rust en Vrede Cab** — $30. "Stellenbosch Cab rivaling $60 Napa."
4. **Ken Forrester Petit Chenin** — $12. "Under $12, genuinely excellent."
5. **Meerlust Rubicon** — $35. "South Africa's most respected red blend."

**WHAT TO SAY:** "Best value white under $15? South African Chenin Blanc. Globally, nothing beats it."
