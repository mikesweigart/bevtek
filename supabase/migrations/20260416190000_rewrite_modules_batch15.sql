**THE STORY**

Australia's Chardonnay reputation was built on the big, oaky, butter-bomb style of the Barossa and Hunter Valley in the 1980s-90s. But a quiet revolution happened: cool-climate regions like Yarra Valley, Adelaide Hills, Mornington Peninsula, and Tasmania started producing lean, mineral, Burgundy-worthy Chardonnay that shocked the wine world.

Today, Australian Chardonnay is a tale of two styles — warm and rich vs. cool and refined — and staff who understand both can match any customer.

