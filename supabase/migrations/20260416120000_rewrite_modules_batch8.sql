**THE STORY**

Rioja is Spain's most famous wine region — and one of the best values in fine wine anywhere. The region has been producing wine since at least Roman times, but its modern identity was forged in the 1860s when French winemakers fled phylloxera (the vine-destroying pest) and brought Bordeaux techniques to Rioja. They introduced oak aging, blending, and quality control — traditions Rioja still follows today.

What makes Rioja special is that bodegas AGE the wine FOR you before releasing it. A Gran Reserva Rioja has spent 5+ years in barrel and bottle before you ever see it on the shelf. Most wine regions release wines young and let consumers age them. Rioja does the work — you get a ready-to-drink, mature wine.

**THE AGING TIERS (printed on every label)**

• **Joven / Cosechero** — no oak, young, fruity. $8-12.
• **Crianza** — 1 year oak + 1 year bottle minimum. Entry serious Rioja. $12-20.
• **Reserva** — 1 year oak + 2 years bottle minimum. The sweet spot. $18-35.
• **Gran Reserva** — 2 years oak + 3 years bottle minimum. Only made in top vintages. $30-120.

**OAK DEBATE: AMERICAN vs. FRENCH**

Traditional Rioja uses **American oak** — gives dill, coconut, sweet vanilla. This is the classic "Rioja taste" that defines the region. Modern producers increasingly use **French oak** — subtler spice, less coconut. Both are valid. Ask: "Do you like vanilla/coconut notes in your red?" American oak lovers = traditional Rioja.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **MUGA Reserva** — $22. The benchmark. Cherry, vanilla, leather, perfectly integrated oak. "The wine that makes people fall in love with Rioja."

2. **La Rioja Alta Viña Ardanza Reserva** — $28. Traditional house. Dried cherry, tobacco, American oak. "Old-school Rioja at its finest."

3. **López de Heredia Viña Tondonia Reserva** — $40. The most traditional producer in Rioja. Released with 10+ years of age. Oxidative, complex, unlike anything else. "The purist's Rioja. Not for everyone — but those who get it, REALLY get it."

4. **Marqués de Cáceres Crianza** — $14. Widely distributed, consistent, food-friendly. "Under $15 Crianza that delivers."

5. **CVNE Imperial Gran Reserva** — $50. Classic Gran Reserva. Already aged 6+ years at release. Dried fruit, leather, cedar. "Ready to drink NOW — that's the whole point of Gran Reserva."

**WHAT TO SAY ON THE FLOOR**

"Like red wine with steak but tired of Cabernet? Rioja Reserva. Oak, leather, structure — different personality, same occasion."

"Value pick for a dinner party? MUGA Reserva, $22. Looks and tastes like a $40 wine."

"What's special about Gran Reserva? They aged it FOR you. That $50 bottle has been sitting in oak and bottle for 6 years already."
