**WHY THIS MATTERS TO YOUR CUSTOMERS**

Two things ruin more wine than corks, vintages, or regions combined: **wrong temperature** and **wrong storage**. Staff who understand this save customers from wasting good bottles — and earn trust that drives repeat visits.

**STORAGE — THE 5 RULES**

1. **Temperature: 55°F (13°C) is ideal.** Anything between 45-65°F is acceptable. Above 70°F = damage within weeks. A bottle left in a hot car trunk in summer can be ruined in HOURS. "Wine is alive. Heat kills it."

2. **Consistency matters more than perfection.** A steady 60°F is better than swinging between 50°F and 70°F. Temperature fluctuations push air through the cork, accelerating oxidation.

3. **Darkness.** UV light degrades wine — it breaks down organic compounds and creates "light strike" (a wet-cardboard smell). This is why wine bottles are usually dark-colored. Never store near windows.

4. **Humidity: 60-70%.** Keeps natural corks moist and sealed. Too dry = cork shrinks, air enters. (Screw-cap wines don't need humidity.)

5. **Position: cork bottles on their side.** Keeps the cork wet from the inside. Screw caps can be stored upright.

**SERVING TEMPERATURE — THE BIGGEST MISTAKE IN WINE**

"Room temperature" is a MYTH from 18th-century European castles (55-60°F). Modern room temperature (72°F) is TOO WARM for any wine. Serving red wine at 72°F mutes the aromas and amplifies the alcohol burn.

**THE ACTUAL TEMPERATURES:**

• **Sparkling:** 40-45°F (ice cold — 15 min in ice bucket)
• **Light whites** (Sauv Blanc, Pinot Grigio): 45-50°F
• **Full whites** (Chardonnay, Viognier): 50-55°F
• **Rosé:** 45-50°F
• **Light reds** (Pinot Noir, Gamay): 55-60°F
• **Medium reds** (Chianti, Rioja): 60-65°F
• **Full reds** (Cabernet, Syrah, Barolo): 63-68°F

**THE 20-MINUTE RULE**

Tell every customer this: "Put your red wine in the fridge for 20 minutes before serving. Take your white wine out of the fridge for 20 minutes before serving." Both arrive at the ideal temperature. Simple, memorable, effective.

**WHAT TO SAY ON THE FLOOR**

"Buying a $50+ bottle? Store it on its side, cool and dark. Don't leave it on top of the fridge — motor heat plus vibration is the worst spot."

"Red wine tonight? Put it in the fridge for 20 minutes before dinner. Sounds wrong, tastes right. Trust me."

"This wine is a gift for next month? Perfect — just keep it in a cool closet, out of sunlight, and it'll be great."
