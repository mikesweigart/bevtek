**THE STORY**

Malbec is one of wine's great reinvention stories. Originally from Cahors in southwestern France (where it was called "Côt" and produced ink-dark, tannic wines), Malbec was always a second-tier Bordeaux blending grape — useful but never the star.

In 1853, Argentine president Domingo Sarmiento hired French agronomist Michel Pouget to bring European grapevines to Mendoza. Among the cuttings: Malbec. What nobody expected was that this grape — mediocre in France — would become extraordinary in the high-altitude sunshine and dry air of the Andes. Argentina's Malbec is softer, fruitier, more approachable, and more consistent than anything France produces from the same grape.

Today, Argentina IS Malbec. 75% of the world's Malbec is grown there.

**WHY ALTITUDE MATTERS**

Mendoza's vineyards range from 2,000 to 5,000+ feet above sea level. Higher altitude means:
• More UV exposure → thicker skins → deeper color + more tannin structure
• Bigger day-night temperature swings → more acid retention → fresher wine
• Lower humidity → fewer diseases → less chemical intervention

**Uco Valley** (3,000-5,000 ft) is where the serious Malbecs come from. Gualtallary, at 5,000+ ft, is producing some of the most exciting wines in the world right now.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Alamos Malbec** — $10. Catena family's entry brand. Dark fruit, soft tannins, reliable. "The $10 wine that makes people fall in love with Malbec."

2. **Catena Malbec** — $18. The benchmark mid-range. More concentration, more oak. "If someone liked Alamos and wants the next level, this is it."

3. **Catena Zapata Malbec Argentino** — $30. High-altitude blend. Violet, blueberry, structure. "Where Malbec starts getting serious."

4. **Susana Balbo Signature Malbec** — $20. First woman to graduate as an enologist in Argentina. Rich, elegant, great value. "A story worth telling + a wine worth drinking."

5. **Trapiche Broquel Malbec** — $14. Widely distributed, consistent quality. "The reliable middle shelf. Never disappoints."

**WHAT TO SAY ON THE FLOOR**

"Steak tonight? Argentine Malbec and Argentine beef were literally made for each other. Catena at $18."

"Budget red that doesn't taste cheap? Alamos Malbec, $10. Can't beat it."

"Napa Cab lover wants value? Malbec from Uco Valley — same power, fraction of the price."
