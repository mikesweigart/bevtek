**WHY THIS MODULE MATTERS MORE THAN ANY OTHER**

Every day, someone walks into your store and says: "I don't know anything about wine. Can you help me?" This moment — right here — determines whether they become a regular customer or never come back.

The #1 rule: **NEVER make them feel stupid.** Wine intimidates people because it has its own language, its own snobbery, and its own hierarchy. Your job is to cut through all of that and match a person to a bottle they'll love. That's it. No lectures. No showing off.

**THE 3-QUESTION METHOD (memorize this)**

You can recommend any wine in the store with just three questions:

1. **"Red, white, or either?"** — Narrows the field by 50%. Simple. Non-threatening.

2. **"What are you eating?"** — Gives you a pairing anchor. If they say "nothing specific" or "just drinking," skip to question 3.

3. **"What's your budget?"** — You need this to recommend honestly. If they don't say a number, point to a range: "Most of our great everyday wines are in this $12-20 range."

**THE UNIVERSALLY-LIKED STARTER WINES**

For customers who say "I don't know" — these are your safe bets:

**Whites:**
• Sauvignon Blanc — crisp, clean, citrus. "Like lemonade for grown-ups."
• Pinot Grigio — light, neutral, inoffensive. "The most easygoing white wine."
• Moscato d'Asti — slightly sweet, bubbly, peach/apricot. 5% ABV. "Wine for people who think they don't like wine."

**Reds:**
• Malbec — soft, fruity, smooth. "The most approachable red."
• Pinot Noir — light, food-friendly, elegant. "Red wine that doesn't punch you in the face."
• Red blend — winemaker already balanced it. "No wrong answer here."

**COMMON OBJECTIONS AND FIXES**

"I don't like dry wine" → Off-dry Riesling, Moscato, or Prosecco.
"Wine gives me headaches" → Likely sulfite sensitivity or dehydration. Try organic/low-sulfite wines. Drink water between glasses.
"I only like sweet wine" → Respect it. Moscato d'Asti, late-harvest Riesling, White Zinfandel.
"Red/white wine is too [strong/acidic/bitter]" → Don't push the opposite. Ask what they DO like and work from there.

**THE $15 SWEET SPOT**

For beginners, $12-18 is the quality inflection point. Below $10, quality drops noticeably. Above $25, the nuances are wasted on a developing palate. Start them in the $12-18 range — they get great wine without overspending on subtlety they can't yet taste.

**WHAT TO SAY ON THE FLOOR**

"Don't know where to start? That's totally fine — that's what I'm here for. What are you having for dinner?"

"You like Moscato? Great choice. When you're ready to try something a little different, Riesling is the next step — similar sweetness, more complexity."

"There are no wrong answers in wine. If you like it, it's good wine. Period."
