**THE STORY**

Tequila is Mexico's most famous export — and it has rules as strict as Champagne. Real tequila can ONLY be made in designated regions of Mexico (mostly Jalisco) from a single plant: the blue Weber agave. That plant takes 6-8 years to mature before harvest. When it's ready, jimadores (agave farmers) use a coa — a flat-bladed tool — to strip the leaves and expose the piña (the heart, which looks like a giant pineapple). The piña is then roasted, crushed, fermented, and distilled.

**HOW IT REACHED AMERICA**

Tequila crossed the border long before the Margarita existed. Mexican-American communities in the Southwest drank it for generations. But the Margarita (invented ~1940s) and Jimmy Buffett's "Margaritaville" (1977) turned tequila into an American cultural icon. The premium tequila boom started in the 2000s when celebrities like George Clooney (Casamigos), Diddy (DeLeón), and The Rock (Teremana) launched brands — proving tequila could be sipped like fine whiskey, not just shot with salt and lime.

**THE CATEGORIES (memorize these)**

• **Blanco / Silver** — unaged or <2 months. Pure agave flavor: pepper, citrus, herbs. BEST for cocktails. This is the Margarita spirit.
• **Reposado** — aged 2-12 months in oak. Softer, with vanilla and light caramel. Good for sipping OR cocktails.
• **Añejo** — aged 1-3 years. Rich oak, caramel, like a soft whiskey. Sip neat.
• **Extra Añejo** — aged 3+ years. Amber, complex, luxury-priced. Special-occasion sipping.
• **Cristalino** — añejo filtered to remove color. Trendy, smooth. Don Julio 70 is the famous one.

**THE MOST IMPORTANT THING ON THE LABEL:** "100% Agave." Without those words, it's "mixto" — up to 49% sugar/grain alcohol. Always steer customers to 100% agave.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Espolón Blanco** — $25. The best value 100% agave blanco in America. Clean, peppery, perfect Margarita tequila. This is your default recommendation when someone says "I need tequila for Margaritas."

2. **Don Julio 1942** — $160. The prestige sipping tequila. Extra añejo, 2.5 years in oak, smooth caramel and vanilla. The bottle is iconic — tall, slender, unmistakable. "Gift for someone who has everything? 1942."

3. **Casamigos Blanco** — $45. George Clooney's former brand (sold to Diageo for $1 billion). Smooth, slightly sweet for a blanco. Very popular, very Instagram. "The celebrity tequila that's actually good."

4. **Patrón Silver** — $42. The original premium tequila. Clean, citrus, smooth. Still the name most customers associate with "good tequila."

5. **Clase Azul Reposado** — $130. The hand-painted ceramic bottle is art. The tequila inside is caramel-rich and smooth. "This is the bottle people display on their bar shelf."

**WHAT TO SAY ON THE FLOOR**

"Margaritas? Espolón Blanco — $25, 100% agave, you'll never go back to mixto."

"Sipping tequila like whiskey? Don Julio 1942 or Clase Azul — both incredible, different price points."

"Is this a gift? Clase Azul. The ceramic bottle alone is a $130 piece of art."
