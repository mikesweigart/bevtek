**THE STORY**

Gin started as medicine. In the 1600s, Dutch physician Franciscus Sylvius created "genever" — a juniper-flavored spirit intended to cure kidney ailments. When English soldiers fighting in the Netherlands discovered it, they brought the taste home. By the 1700s, London was drowning in cheap gin — the infamous "Gin Craze" killed thousands and led to the Gin Act of 1751.

From that chaos emerged London Dry Gin: a clean, carefully distilled spirit that became the foundation of the British Empire's cocktail culture. The Gin & Tonic was invented in India as a way to make anti-malaria quinine (tonic water) drinkable.

**HOW IT REACHED AMERICA**

Prohibition paradoxically helped gin. "Bathtub gin" was easy to make at home (just redistill neutral spirit with juniper and botanicals). After Prohibition ended, the Martini became America's power-drink — and gin was its backbone. The 1990s-2000s saw gin decline as vodka took over, but the craft cocktail revival of the 2010s brought gin roaring back. Today's "New Western" gins (Hendrick's, Aviation, Monkey 47) have made gin the hipster-bartender's spirit of choice.

**THE STYLES**

• **London Dry** — juniper-forward, dry, crisp. The classic. Must be distilled with all botanicals simultaneously (no post-distillation flavoring). Tanqueray, Beefeater, Sipsmith.
• **Plymouth** — softer, earthier, slightly less juniper. Only one distillery makes it (in Plymouth, UK).
• **Old Tom** — slightly sweeter historical style. Used in Martinez and Tom Collins. Hayman's.
• **New Western / Contemporary** — juniper present but other botanicals dominate. Hendrick's (rose + cucumber), Aviation, Monkey 47.
• **Genever** — the Dutch original. Malted grain base. Halfway between whiskey and gin.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Tanqueray London Dry** — $22. The bartender's gin. Bold juniper, four botanicals (juniper, coriander, angelica, licorice). The default for G&Ts and Negronis. "If a recipe says 'gin,' this is what they mean."

2. **Hendrick's** — $35. Rose petals + cucumber. The gin that made people who "hate gin" love gin. Distinctive dark bottle. "Serve with cucumber instead of lime."

3. **Beefeater London Dry** — $20. Classic, citrus-forward, excellent value. The go-to well gin in serious bars. Nine botanicals.

4. **Monkey 47** — $50. Black Forest gin with 47 botanicals. Complex, herbal, wildly interesting. Great gift bottle. "For the person who thinks they've tried every gin."

5. **Aviation** — $28. Ryan Reynolds' brand (bought from Portland distiller). Juniper-light, floral, soapy in a good way. New Western style.

**WHAT TO SAY ON THE FLOOR**

"G&T at home? Tanqueray + Fever-Tree tonic + lime. Under $35 for everything."

"Gift for a gin lover? Monkey 47 — $50, they'll geek out over the 47 botanicals."

"Customer says 'I don't like gin': hand them Hendrick's. Cucumber and rose. It changes minds."
