**THE STORY**

Champagne isn't just a wine — it's a legal designation. Only sparkling wine from the Champagne region of northeastern France (about 90 miles northeast of Paris) can legally be called "Champagne." Everything else is "sparkling wine," no matter how good.

The monk Dom Pérignon is often credited with "inventing" Champagne in the 1600s, but he actually tried to PREVENT the bubbles (they kept exploding barrels). The real innovation was the English: they developed stronger glass bottles and cork stoppers that could contain the pressure. By the 1800s, Champagne had become the drink of royalty, celebration, and power.

**THE METHOD**

What makes Champagne special is the **méthode champenoise** (traditional method): a second fermentation happens INSIDE the bottle, creating fine, persistent bubbles. This is expensive, slow, and produces a texture no other method matches.

• **Champagne** — traditional method, Champagne region. $40-500+.
• **Prosecco** — tank method (Charmat), Italy. Big frothy bubbles, apple/pear. $12-30.
• **Cava** — traditional method, Spain. Great Champagne alternative. $10-25.
• **Crémant** — traditional method, other French regions (Loire, Alsace). $18-35.

**LABEL TERMS STAFF MUST KNOW**
• **Brut** — dry (standard, 90% of Champagne sold)
• **Extra Brut** — very dry
• **Brut Nature / Zero Dosage** — bone dry
• **Extra Dry** — confusingly, SWEETER than Brut (off-dry)
• **Blanc de Blancs** — 100% Chardonnay (lighter, elegant)
• **Blanc de Noirs** — 100% Pinot Noir/Meunier (richer, more body)
• **Rosé** — pink Champagne, usually more expensive

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Veuve Clicquot Brut (Yellow Label)** — $55. The default prestige Champagne. Everyone knows the yellow label. Rich, toasty, reliable. "The go-to celebration bottle. You cannot go wrong."

2. **Moët & Chandon Impérial** — $45. The world's best-selling Champagne. Apple, citrus, toast. Lighter than Veuve. "Brunch, toast, party — Moët is always appropriate."

3. **Dom Pérignon** — $250+. The prestige cuvée from Moët. Only made in exceptional vintages. Complex, powerful, ages beautifully. "When nothing else will do."

4. **Taittinger Brut Réserve** — $48. Chardonnay-dominant, elegant, refined. Less well-known than Veuve but often preferred by wine people. "The sommelier's Champagne."

5. **La Marca Prosecco** — $14. The #1 Prosecco in America. Light, bubbly, apple-pear. "Mimosas, Aperol Spritzes, everyday sparkle — this is it."

**WHAT TO SAY ON THE FLOOR**

"Celebration? Veuve Clicquot. Nobody has ever been disappointed opening a bottle of Veuve."

"Brunch mimosas? Don't waste Champagne — grab La Marca Prosecco at $14. Save the Champagne for the toast."

"Champagne taste, Prosecco budget? Cava. Same method as Champagne, $12-15. It's Spain's best-kept secret."
