**THE STORY**

Bordeaux is the most famous wine region in the world — and has been for centuries. The marriage of Eleanor of Aquitaine to England's Henry II in 1152 gave the English access to Bordeaux wine, launching a 900-year love affair. The 1855 Classification — commissioned by Napoleon III for the Paris World's Fair — ranked Bordeaux's top estates into five "growths" (crus), a hierarchy that still defines the market today.

Bordeaux matters because it INVENTED the modern wine industry: classification, château branding, vintage variation, critic scores, auction pricing — all Bordeaux innovations.

**LEFT BANK vs. RIGHT BANK**

The Gironde estuary splits Bordeaux into two halves with completely different soils, grapes, and personalities:

**Left Bank** (Médoc, Pauillac, Margaux, Saint-Julien): Gravel soils. **Cabernet Sauvignon** dominates (65-85% of blends). Structured, firm tannins. Bold, dark fruit, cedar, tobacco. Needs 5-15 years to open up. The "serious" side. Pairings: ribeye, lamb.

**Right Bank** (Saint-Émilion, Pomerol): Clay and limestone soils. **Merlot** dominates (70-90%). Softer, rounder, plum and cocoa notes. Drinks well younger. The "approachable" side. Pairings: duck, pork, mushrooms.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Château Meyney Saint-Estèphe** — $30. Left Bank, honest Bordeaux at an honest price. Dark fruit, firm structure, food-friendly. "This is what $30 Bordeaux SHOULD taste like."

2. **Château Canon Saint-Émilion** — $70. Right Bank Grand Cru Classé. Plum, violets, silk. "The Right Bank wine that converts people who think they only like Left Bank."

3. **Château Lynch-Bages Pauillac** — $100. 5th Growth that drinks like a 2nd Growth. Bold, complex, iconic. "The overachiever of Bordeaux — always punches above its classification."

4. **Mouton Cadet** — $15. Baron Philippe de Rothschild's everyday Bordeaux. Light, fruity, simple. The entry-level. "Want to try Bordeaux without committing? Start here."

5. **Château Margaux** — $500+. First Growth. One of five wines at the absolute peak of Bordeaux. "When someone wants THE BEST, this is the conversation."

**WHAT TO SAY ON THE FLOOR**

"Never had Bordeaux? Mouton Cadet at $15. No risk, honest wine."

"Steak dinner and want to impress? Lynch-Bages — $100, punches like a $200 wine. The story sells itself."

"Left Bank or Right Bank? Steak = Left Bank (Cabernet). Duck or pork = Right Bank (Merlot). Both are Bordeaux, just different personalities."
