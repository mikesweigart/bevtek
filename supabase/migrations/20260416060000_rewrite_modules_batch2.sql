**THE STORY**

Napa Valley wasn't always America's wine capital. In the 1960s-70s, it was a sleepy agricultural valley with more cattle than vines. Everything changed on May 24, 1976 — the "Judgment of Paris." British wine merchant Steven Spurrier organized a blind tasting in Paris where French judges rated wines. A 1973 Stag's Leap Wine Cellars Cabernet Sauvignon beat every Grand Cru Bordeaux. Napa Valley went from unknown to world-class overnight.

Today, Napa produces some of the most sought-after Cabernet Sauvignon on Earth. Warm days, cool nights (Pacific fog rolls in through the Petaluma Gap), volcanic and alluvial soils, and 30+ distinct sub-AVAs create an extraordinary range of styles.

**WHY IT'S EXPENSIVE**

Napa Valley land costs $300,000-$500,000 per acre (vs. $15,000 in Paso Robles). Labor is expensive. Yields are deliberately low (quality over quantity). The brand premium is real — "Napa Cabernet" carries global cachet that no other American region matches.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Silver Oak Alexander Valley** — $65. The Napa Cab that every steakhouse carries. Exclusively American oak, which gives it a signature dill-vanilla-coconut note. Rich, smooth, crowd-pleasing. Not actually Napa (Alexander Valley is Sonoma County), but everyone thinks of it as "Napa-style."

2. **Caymus Vineyards Napa** — $85. The cult-everyday Cab. Dark fruit, chocolate, plush tannins. Consistently high-rated, consistently in demand. "The bourbon of Napa Cab — everyone knows it, everyone respects it."

3. **Opus One** — $400+. The Robert Mondavi + Baron Philippe de Rothschild collaboration. Half Napa, half Bordeaux DNA. The ultimate prestige California wine. "This is what people buy to celebrate something they'll remember forever."

4. **Stag's Leap Artemis** — $55. From the district that won the Judgment of Paris. Elegant, silky, violets and dark cherry. "The wine that changed everything in 1976."

5. **Jordan Cabernet Sauvignon** — $60. Alexander Valley. Bordeaux-inspired, restrained, food-friendly. Not a fruit bomb — a wine for people who find most Napa Cabs too big.

**WHAT TO SAY ON THE FLOOR**

"Steak dinner, no specific preference? Silver Oak or Caymus. Both under $90, both guaranteed crowd-pleasers."

"Gift for a wine collector? Opus One if budget allows. Otherwise, Stag's Leap Artemis — $55, the Judgment of Paris story sells itself."

"Customer says 'Napa Cab is too bold for me': Jordan — Bordeaux-style, more finesse."
