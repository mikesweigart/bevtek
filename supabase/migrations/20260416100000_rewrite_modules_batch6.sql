**THE STORY**

Every white Burgundy you've ever heard of — Chablis, Meursault, Puligny-Montrachet, Pouilly-Fuissé — is 100% Chardonnay. France doesn't put the grape on the label; they put the village. This confuses Americans endlessly. But once you crack the code, French Chardonnay becomes a treasure map.

Chardonnay is often called a "blank canvas" grape — it takes on the character of where it's grown and how it's made more than almost any other variety. Cold, chalky Chablis? Steel, mineral, citrus. Warm, limestone Meursault? Rich, buttery, hazelnut. Same grape. Completely different wines.

**THE THREE STYLES**

**1. Chablis (northern Burgundy)** — UNOAKED. Stainless steel. Flinty, salty, razor-sharp acid. The classic oyster wine. Premier Cru and Grand Cru Chablis gain complexity without losing that electric minerality. $20-100.

**2. Côte de Beaune (heart of Burgundy)** — OAK-AGED. Meursault, Puligny-Montrachet, Chassagne-Montrachet. Rich, creamy, hazelnut, lemon curd, butter. THE gold standard for "serious" Chardonnay. $50-500+.

**3. Mâconnais (southern Burgundy)** — LIGHT OAK to none. Pouilly-Fuissé, Saint-Véran, Mâcon-Villages. Fruit-forward, balanced, great value. The entry point. $15-40.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Louis Jadot Mâcon-Villages** — $15. Clean, apple, light honey. "Under $15 French Chardonnay that tastes like $25. The everyday pick."

2. **William Fèvre Chablis** — $22. Benchmark Chablis. Crisp, mineral, flinty. "Oyster night? This. No discussion."

3. **Joseph Drouhin Meursault** — $55. Golden, hazelnut, butter, oak. "The Meursault experience without the Montrachet price tag."

4. **Louis Latour Pouilly-Fuissé** — $28. Mâconnais' best. Richer than Mâcon-Villages, more elegant than Côte de Beaune. "The Goldilocks French Chard — not too lean, not too fat."

5. **Domaine Leflaive Puligny-Montrachet** — $120+. The pinnacle. Mineral, tension, purity. "For the collector. This is white wine at its absolute peak."

**WHAT TO SAY ON THE FLOOR**

"Love oaky California Chard? Meursault is the French version — but with more finesse."

"Hate oaky Chard? Chablis. Unoaked, mineral, crisp. It'll change your mind about Chardonnay."

"Budget pick? Mâcon-Villages, $15. Tastes French because it IS French."
