**THE SKILL THAT SEPARATES GREAT FROM GOOD**

The best liquor store employees read a customer within 10 seconds of them walking through the door. They adjust their approach — hovering vs. stepping back, advising vs. processing — based on who's in front of them. This isn't magic. It's pattern recognition. And it can be learned.

**THE FOUR CUSTOMER TYPES**

**1. THE BROWSER** — no specific goal, just looking.
*Body language:* wandering slowly, picking up bottles, reading labels, putting them back.
*What they want:* space. Don't pounce.
*Your move:* after 2-3 minutes, casual check-in: "Finding everything okay? Let me know if anything catches your eye." If they engage, shift to Advisor mode. If they don't, back off.

**2. THE MISSION BUYER** — knows exactly what they want.
*Body language:* walks straight to a section, scanning quickly, checking their phone (probably a list or text from someone).
*What they want:* speed and efficiency. Don't upsell.
*Your move:* "Can I help you find something?" Walk them to it. Done. If they buy bourbon AND mention dinner, one soft suggestion: "That's a great pick. Making Old Fashioneds? Grab these bitters." That's it.

**3. THE ASKER** — wants guidance, open to suggestions.
*Body language:* looking around, hesitating, comparing two bottles, sometimes with a slightly lost expression.
*What they want:* YOUR expertise. They came here INSTEAD of a supermarket because they want a human recommendation.
*Your move:* "What's the occasion?" → listen → recommend with confidence. This is your chance to shine. Recommend freely. They'll spend more than they planned and thank you for it.

**4. THE ENTHUSIAST** — knows a lot, wants to talk shop.
*Body language:* immediately picks up premium bottles, uses terms like "single barrel," "terroir," "natural wine," asks about allocation.
*What they want:* a peer, not a salesperson. They want someone who matches their knowledge.
*Your move:* go deep. Share what you know. Show them something new. "Have you tried the Amrut Fusion? Indian single malt — it's winning blind tastings." They'll become a regular if you impress them.

**THE CRITICAL INSIGHT**

Browsers and Mission Buyers want to be LEFT ALONE. Askers and Enthusiasts want ENGAGEMENT. Mismatching the approach is the #1 service error in beverage retail:
• Hovering over a Browser = creepy
• Ignoring an Asker = lost sale
• Talking down to an Enthusiast = insulting
• Rushing a Browser into a decision = pressuring

**Train your instinct:** within 10 seconds, decide which type. Adjust. It becomes automatic within a month.

**WHAT TO SAY ON THE FLOOR**

Walk-in, heads straight to whiskey, phone out → Mission Buyer. "Hi! Looking for something specific? I can point you right to it."

Walk-in, drifting through wine, picking up bottles → Browser. Give space. Wait 2 minutes. "Finding some good options?"

Walk-in, standing at the Champagne wall looking overwhelmed → Asker. "Picking out Champagne? I can help — what's the occasion?"
