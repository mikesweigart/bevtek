**THE STORY**

Burgundy is where Pinot Noir becomes religion. This narrow strip of eastern France — just 30 miles long and rarely more than a mile wide — produces the most celebrated (and expensive) Pinot Noir on Earth. The monks of Cîteaux and Cluny monasteries mapped these vineyards 900 years ago, walking the rows, tasting the soil, creating the concept of "terroir" — the idea that WHERE a vine grows matters more than HOW it's grown.

What makes Burgundy maddening is also what makes it magical: two neighboring plots, separated by a dirt path, can produce radically different wines. One is silky and floral, the other is structured and dark. Same grape, same winemaker, same vintage. Different patch of dirt. That's Burgundy.

**WHY IT'S SO EXPENSIVE**

Supply and demand at its most extreme. The best vineyards are TINY — Romanée-Conti (arguably the world's most famous vineyard) is just 4.5 acres. Total production: ~6,000 bottles per year. For the entire planet. Meanwhile, global demand from collectors in the US, UK, China, and Japan has exploded. Prices have doubled since 2015.

**THE QUALITY PYRAMID (print this in your brain)**

1. **Bourgogne Rouge** — regional. Simple, fruity, $15-25. Entry point.
2. **Village** (e.g., "Gevrey-Chambertin") — named after a town. Better complexity. $30-60.
3. **Premier Cru (1er Cru)** — named vineyard within a village. Serious quality. $60-150.
4. **Grand Cru** — the peak. Only ~2% of all Burgundy. $150-$15,000+.

**KEY VILLAGES (North = bolder, South = softer)**

• **Gevrey-Chambertin** — the biggest, most structured Pinot. Dark cherry, earth, power.
• **Vosne-Romanée** — home of Romanée-Conti. Silky, perfumed, ethereal.
• **Nuits-Saint-Georges** — muscular, earthy, good value for Burgundy.
• **Pommard** — sturdy, dark-fruited, tannic for Pinot.
• **Volnay** — elegant, floral, feminine. "The prettiest Burgundy."

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Louis Jadot Bourgogne Rouge** — $18. The reliable entry-level. Widely available, consistent. "Want to try real Burgundy without spending $60? Start here."

2. **Joseph Drouhin Gevrey-Chambertin** — $50. Village-level from a great house. Dark cherry, earth, structured. "This is where you understand why people obsess over Burgundy."

3. **Domaine Faiveley Mercurey** — $28. From the Côte Chalonnaise (south of the main Côte d'Or). Genuine Burgundy character at a fraction of the price. "The insider's value pick."

4. **Bouchard Père et Fils Beaune 1er Cru** — $45. Premier Cru at an accessible price. Elegant, balanced. "For someone ready to step into the Premier Cru tier."

5. **Albert Bichot** — $20-40 range. Multiple village and regional bottlings. Reliable, well-distributed. "A house you can trust at every price point."

**WHAT TO SAY ON THE FLOOR**

"Burgundy curious? Louis Jadot Bourgogne Rouge at $18. Real Burgundy, real quality, zero intimidation."

"Ready to invest? Joseph Drouhin at the village level — $50 gets you named-village complexity."

"Why is that one $300? It's Premier or Grand Cru — a single vineyard that's been famous for 500 years. You're buying history."
