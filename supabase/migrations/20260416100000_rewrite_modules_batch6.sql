**THE STORY**

The Rhône Valley is France's second-most-important wine region after Bordeaux — and for everyday drinking, many sommeliers argue it's MORE important. While Bordeaux aims for prestige, the Rhône delivers pleasure at every price point.

The region splits into two halves along the Rhône river:

**Northern Rhône** — steep granite hillsides. TINY production. 100% Syrah reds. Côte-Rôtie, Hermitage, Cornas, Saint-Joseph, Crozes-Hermitage. Serious, age-worthy wines for collectors and sommeliers.

**Southern Rhône** — wider, flatter, warmer. LARGE production. Grenache-dominant blends (GSM: Grenache, Syrah, Mourvèdre). Châteauneuf-du-Pape is the star; Côtes du Rhône is the everyday workhorse. Generous, warm, spicy wines for everyone.

**WHY THE RHÔNE MATTERS FOR YOUR STORE**

Côtes du Rhône is arguably the best-value red wine category in the world. At $12-18, you get honest, food-friendly, well-made wine that punches WAY above its price. Every store needs a good CDR section.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **E. Guigal Côtes du Rhône** — $14. The benchmark everyday Rhône. Raspberry, pepper, herbs. "The $14 wine that drinks like $25. Best value red in the store."

2. **Châteauneuf-du-Pape (various)** — $35-80. The Southern Rhône flagship. Warm, spicy, powerful, often 14-15% ABV. Château de Beaucastel ($75), Vieux Télégraphe ($60), Domaine du Vieux Lazaret ($30).

3. **Crozes-Hermitage** — $18-30. Entry-level Northern Rhône. 100% Syrah, black pepper, smoke, violets. "Australian Shiraz fans: try this. Same grape, French elegance."

4. **M. Chapoutier Bila-Haut** — $12. Côtes du Roussillon (southern France, Rhône-style). Incredible value. Dark fruit, garrigue herbs. "Under $12 and genuinely good."

5. **Gigondas** — $22-35. "Baby Châteauneuf" — similar style, lower price. "The sommelier's secret for people who love Châteauneuf but not the price."

**WHAT TO SAY ON THE FLOOR**

"Everyday red, budget-friendly? Guigal Côtes du Rhône — $14, never disappoints."

"Loves bold Australian Shiraz? Try Northern Rhône Syrah. Same grape, more pepper, more finesse."

"Special occasion, wants French but not Bordeaux? Châteauneuf-du-Pape. Power, warmth, and history in every sip."
