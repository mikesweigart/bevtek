**WHY THIS IS YOUR SUPERPOWER**

Gift purchases are 20-30% of a liquor store's revenue during the holidays — and a significant chunk year-round (birthdays, housewarmings, thank-you gifts, corporate). The staff member who can instantly match budget → perfect bottle → gift bag CLOSES sales that other stores lose.

**UNDER $25 — "Something thoughtful without overthinking it"**

• **Prosecco ($12-18)** — La Marca, Mionetto. Universally liked, festive, non-committal.
• **Jameson Irish Whiskey ($30... but often $25 on promo)** — safe, classic, everyone recognizes it.
• **Aperol ($22) + any Prosecco ($12)** — wrap together as a "Spritz Kit" = $34 perceived as $50.
• **Nice wine in a gift bag ($15-20 wine + $2 bag)** — a $17 Côtes du Rhône in tissue paper feels like a $30 gift.

**$25-50 — "I want it to feel special"**

• **Woodford Reserve Bourbon ($35)** — beautiful bottle, universally respected, looks expensive.
• **Hendrick's Gin ($35)** — distinctive dark bottle, rose + cucumber, sophisticated.
• **Veuve Clicquot ($55)** — THE celebration Champagne. The yellow label IS the gift.
• **Maker's Mark gift set with glasses ($40)** — premade, no wrapping needed.

**$50-100 — "I want to impress"**

• **Glenmorangie 18 ($75)** — elegant Highland Scotch, gorgeous tube packaging.
• **WhistlePig 10 Year Rye ($75)** — whiskey-lover territory, 100% rye, craft credentials.
• **Patrón Añejo ($65)** — impressive bottle, sippable tequila, premium image.
• **Hibiki Harmony ($75)** — Japanese whisky in a 24-faceted bottle. Art + liquid.

**$100+ — "Money is not the issue"**

• **Don Julio 1942 ($160)** — THE prestige tequila. Instantly recognized.
• **Johnnie Walker Blue ($200)** — the "I want the best Scotch" answer. Always lands.
• **Dom Pérignon ($250+)** — the ultimate celebration. Nothing else needed.
• **Clase Azul Reposado ($130)** — hand-painted ceramic bottle that becomes a vase. As much art as tequila.

**THE GIFT WRAPPING MULTIPLIER**

A $20 wine in a nice gift bag with tissue paper = perceived as $35-40 gift. The bag costs you $1-2. ALWAYS offer it. "Would you like a gift bag? We have them right here."

**WHAT TO SAY ON THE FLOOR**

"It's a gift? Great — what's your budget? I'll find something perfect." (DON'T ask them to browse. GUIDE them.)

"Under $30 and you want a guaranteed win? Woodford Reserve. Beautiful bottle, $35, everyone loves it."

"Stuck? Champagne. Always. $45-60 for Veuve or Moët. No one has EVER been disappointed receiving Champagne."

"Corporate gift for someone you don't know well? Champagne or premium olive oil + wine combo. Safe, classy, inclusive."
