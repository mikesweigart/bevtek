**THE STORY**

Cognac is brandy — but not all brandy is Cognac. Just like Champagne, the name is legally protected: only brandy from the Cognac region of southwestern France, made from Ugni Blanc grapes, double-distilled in copper pot stills, and aged in French oak, can be called Cognac.

The story begins in the 1600s when Dutch traders visited the Charente region to buy wine. The local white wine was thin and sour — terrible for shipping. But when distilled ("brandewijn" — burnt wine — shortened to "brandy"), it became concentrated, shelf-stable, and delicious. The French refined the process over centuries into the world's most prestigious aged spirit.

**HOW IT REACHED AMERICA**

Cognac was America's luxury spirit long before whiskey existed. In the 1800s, it was the base for the Sazerac (New Orleans' signature cocktail), the Sidecar, and the Brandy Alexander. The phylloxera epidemic of the 1870s devastated Cognac's vineyards, opening the door for Scotch to take over. But Cognac never lost its prestige.

In the 1990s-2000s, Hennessy and hip-hop culture created a second American boom. Rappers from Tupac to Jay-Z referenced Hennessy by name, making it a cultural icon in Black American communities. Today, the US is Cognac's #1 export market.

**THE AGE CLASSIFICATIONS (memorize these)**

• **VS (Very Special)** — aged at least 2 years. Young, fiery, good for cocktails. $30-40.
• **VSOP (Very Superior Old Pale)** — aged at least 4 years. Smoother, sipping-worthy. $45-70.
• **XO (Extra Old)** — aged at least 10 years (changed from 6 in 2018). Complex, rich, special-occasion. $100-300+.
• **Hors d'Âge** — beyond age classification. The pinnacle. $200+.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Hennessy VS** — $40. The world's best-selling Cognac. Smooth, fruity, versatile. "The default. Cocktails or on ice."

2. **Rémy Martin VSOP** — $50. Exclusively Fine Champagne Cognac (top two growing zones). Vanilla, stone fruit, honeyed. "The step-up sip. Beautiful neat."

3. **Hennessy XO** — $200. 100+ eaux-de-vie blended. Chocolate, spice, dried fruit. "The prestige gift. When you need to make an impression."

4. **Courvoisier VSOP** — $35. Light, floral, elegant. "Napoleon's Cognac — literally. He reportedly brought it to exile."

5. **Pierre Ferrand 1840** — $35. Craft Cognac, designed for cocktails. "Bartender favorite. Makes the best Sidecar and Sazerac you've ever had."

**WHAT TO SAY ON THE FLOOR**

"Want to try Cognac? Hennessy VS on ice — $40. Easy, smooth, no intimidation."

"Gift for someone sophisticated? Rémy XO or Hennessy XO. The bottle alone says everything."

"Making cocktails? Pierre Ferrand 1840 — it's designed for mixing. Sidecars, Sazeracs, Between the Sheets."

"Bourbon lover curious about Cognac? They share DNA — both are aged in oak, both are caramel-vanilla-fruit. The connection is real."
