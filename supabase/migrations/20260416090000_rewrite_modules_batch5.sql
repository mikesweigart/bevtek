**WHY THIS MATTERS**

Upselling isn't about squeezing more money from a customer. It's about solving their problem BETTER. When you suggest Cointreau over cheap triple sec for a Margarita, you're not being pushy — you're genuinely improving their experience. That's the difference between sales and service.

The best liquor store employees in the world don't "sell." They advise. They listen. They match the product to the person. And in doing so, they naturally increase ticket size — not by pushing, but by helping.

**THE 5 TECHNIQUES THAT ACTUALLY WORK**

**1. LISTEN FIRST, RECOMMEND SECOND**
"What's the occasion?" reveals everything: budget, formality, taste preferences, and whether this is for them or a gift. NEVER recommend before asking this question.

**2. OFFER ONE STEP UP — NOT THREE**
Customer picks a $15 wine → suggest the $22 version, not the $50 one. "That's a solid choice. For a few dollars more, this one has a bit more complexity." The customer feels guided, not pressured.

**3. BUNDLE, DON'T UPGRADE**
Instead of "buy the more expensive bourbon," say: "That bourbon makes an incredible Old Fashioned. All you need is Angostura bitters ($10) and Luxardo cherries ($22). I can walk you to them." Now they're buying 3 items instead of 1, and they're GRATEFUL.

**4. USE THE GIFT TEST**
"Is this for you or a gift?" Gift buyers have implicit permission to spend more. They're buying an impression, not just a bottle. Gift = nicer shelf, prettier bottle, gift bag.

**5. VALIDATE THEIR FIRST CHOICE**
NEVER say "oh, you don't want THAT." Always say: "That's great — and if you want to explore something different, this is what I'd suggest." Respect their choice. Add to it. Don't replace it.

**WHAT NEVER TO DO**

• Don't push based on YOUR margin. Push based on THEIR experience.
• Don't upsell when someone is clearly on a tight budget. Read the room.
• Don't use price as a quality shorthand ("this $50 one is sooo much better").
• Don't make them feel judged for their choice.

**THE METRIC**

A good upsell feels like ADVICE, not a sales pitch. If the customer says "thanks, that's a great idea" — you did it right. If they feel awkward or pressured, you went too far.

**REAL EXAMPLES FROM THE FLOOR**

Customer buying Jim Beam → "Jim Beam is solid. If you like it and ever want to try the step up, Buffalo Trace at $25 is really special." (One step up, not five.)

Customer buying Aperol → "Making Spritzes? Grab a Prosecco and some oranges — you're all set for the afternoon." (Bundle — 3 items, grateful customer.)

Customer buying wine for a dinner party → "How many guests? For 6 people, I'd do 2 reds and 1 white. Here's what I'd pick at your budget..." (Advisor mode — they came for 1 bottle, leaving with 3.)
