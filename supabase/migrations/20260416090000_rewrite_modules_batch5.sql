**WHY PAIRING MATTERS**

A customer walks in and says "I'm making salmon tonight — what wine should I get?" If your answer is "uh, probably a white?" — you've lost them. If your answer is "Pinot Noir, actually — Oregon Pinot is the classic salmon pairing. We have A to Z for $18 or Domaine Drouhin for $45" — you've made a fan for life.

Pairing knowledge is the single most powerful selling tool in beverage retail. It turns a commodity purchase into an expert consultation.

**THE THREE RULES THAT ACTUALLY WORK**

**1. MATCH INTENSITY.** Light food (salads, white fish) with light drinks (Sauvignon Blanc, Pilsner). Rich food (braised short ribs, blue cheese) with big drinks (Cabernet, Imperial Stout). Don't overthink it — weight-matching works 80% of the time.

**2. COMPLEMENT OR CONTRAST.** Sweet Riesling with spicy Thai food = contrast (sweetness calms heat). Oaky Chardonnay with buttery lobster = complement (both are rich). Either approach works — just be intentional.

**3. REGIONAL PAIRING.** What grows together goes together. Chianti with Tuscan pasta. Rioja with paella. IPA with fish tacos. Centuries of co-evolution produced these pairings for a reason.

**THE CHEAT SHEET (memorize this)**

**STEAK:** Napa Cab, Bordeaux, Malbec, Barossa Shiraz
**SALMON:** Oregon Pinot Noir, Rosé, Chardonnay (oaked)
**CHICKEN:** literally anything (it's neutral)
**SPICY FOOD:** off-dry Riesling (sweetness tames heat), Mexican lager, Gewürztraminer
**PIZZA:** Chianti (Sangiovese), Barbera, any pale ale
**SUSHI:** dry Riesling, Champagne, Japanese lager (Sapporo, Asahi)
**OYSTERS:** Muscadet, Chablis, Champagne, GUINNESS (yes, really)
**BBQ:** Zinfandel, Syrah/Shiraz, IPA
**THANKSGIVING:** Oregon Pinot Noir + Riesling + Beaujolais covers EVERYTHING
**CHOCOLATE:** Port, Zinfandel, Imperial Stout, Bourbon
**CHEESE:** see the Wine & Cheese module (but quick version: goat cheese = Sancerre, blue = Sauternes/Port, aged cheddar = Cab)

**THE UNIVERSAL SAFE BETS**

• **Champagne goes with almost everything** (including fried chicken — seriously)
• **Off-dry Riesling bridges sweet-savory divides**
• **Pinot Noir pairs with more food than any other red**
• **Pilsner pairs with most savory food**

**WHAT TO SAY ON THE FLOOR**

Customer mentions dinner → "What are you having?" → match the protein and sauce. That's it.

"Hosting Thanksgiving for 12? Oregon Pinot ($18-25), an off-dry Riesling ($15), and a Beaujolais ($15). Three bottles cover every dish on the table."

"BBQ this weekend? Zinfandel. The smoky, spicy fruit loves grilled meat."

"Don't overthink it. A wine the MEAL enjoys is more important than a wine the LABEL impresses."
