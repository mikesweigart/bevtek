**THE STORY**

Liqueurs are the secret ingredients that turn a good cocktail into a great one. They're sweetened, flavored spirits — lower proof than base spirits, higher impact on flavor. Monks invented most of them (Chartreuse, Bénédictine) as herbal medicines in the Middle Ages. Italian amari (plural of amaro) started as digestive tonics — bitter herbs steeped in alcohol to settle the stomach after a meal.

Today, the Negroni (which requires Campari), the Margarita (which needs Cointreau), and the Espresso Martini (Kahlúa or Mr Black) are among the world's most-ordered drinks. Without liqueurs, the cocktail world shrinks by 60%.

**THE CATEGORIES YOUR STAFF MUST KNOW**

• **Orange liqueurs:** Cointreau ($35), Grand Marnier ($38), dry curaçao. Essential for Margaritas, Sidecars, Cosmos.
• **Italian bitter / aperitivo:** Campari ($28) — Negroni. Aperol ($25) — Spritz. Non-negotiable.
• **Amaro (Italian bitter digestifs):** Averna ($28), Montenegro ($30), Fernet-Branca ($30), Amaro Nonino ($45). Sip after dinner or build cocktails.
• **Coffee:** Kahlúa ($25), Mr Black ($35). Espresso Martinis, White Russians.
• **Elderflower:** St-Germain ($35). The bartender's ketchup — goes with everything.
• **Herbal:** Chartreuse Green ($60), Yellow ($50). 130 botanicals, made by monks, irreplaceable.
• **Maraschino:** Luxardo ($30). Not cherry flavor — it's a clear, dry, almondy liqueur. Essential for Last Word, Aviation, Hemingway Daiquiri.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Campari** — $28. Red, intensely bitter, 24% ABV. No Negroni exists without it. "If they're making Negronis, Campari is not optional. There is no substitute."

2. **Cointreau** — $35. The premium triple sec. Clean orange oil, dry. "The Margarita upgrade everyone needs. Cointreau vs. cheap triple sec is the biggest flavor jump in cocktails."

3. **Aperol** — $25. Campari's softer cousin. 11% ABV, orange, gentian. "Aperol Spritz — 3 Prosecco, 2 Aperol, 1 soda. Outsells everything in summer."

4. **St-Germain** — $35. Elderflower, floral, sophisticated. "Add ½ oz to literally any cocktail. It makes everything better."

5. **Amaro Montenegro** — $30. The gateway amaro. Orange peel, vanilla, herbs, balanced bitterness. "After-dinner sip. Italians have one every night. Try it on ice with an orange peel."

**WHAT TO SAY ON THE FLOOR**

"Customer bought gin and Campari? Ask: 'Making Negronis? You'll want sweet vermouth — Carpano Antica transforms it.' Now they're buying 3 bottles."

"Someone browsing the liqueur section looking lost? 'What cocktails do you like to make?' leads the conversation."

"Gift for a cocktail person? Chartreuse Green — $60, iconic, and they probably don't already have it."
