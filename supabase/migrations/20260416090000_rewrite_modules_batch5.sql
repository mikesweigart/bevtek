**THE STORY**

Porter was London's first mass-produced beer — invented in the 1720s for the porters (manual laborers) who carried goods through the city's markets. It was dark, robust, cheap, and sustaining. "Stout porter" was the stronger version — eventually shortened to just "stout."

Arthur Guinness brewed his first stout at St. James's Gate in Dublin in 1759. That brewery is still running. Guinness became synonymous with stout worldwide — and the nitro pour (invented in 1959 by Guinness mathematician Michael Ash) gave stout its signature creamy, cascading head.

**THE STYLES**

• **Dry Irish Stout** — Guinness territory. Low ABV (4-5%), coffee, dry finish, nitro pour. Light-bodied despite dark appearance. "People think stout is heavy. Guinness has fewer calories than orange juice."

• **Oatmeal Stout** — oats add silky mouthfeel. 5-6% ABV. Samuel Smith's Oatmeal Stout, Founders Breakfast Stout.

• **Milk Stout** — lactose adds sweetness and body. 5-7% ABV. Left Hand Milk Stout (famous for its nitro version).

• **Imperial / Russian Imperial Stout** — the big one. 8-12%+ ABV. Molasses, dark chocolate, espresso. Barrel-aged versions are collectible. North Coast Old Rasputin, Founders KBS, Goose Island Bourbon County.

• **Pastry Stout** — imperial stout with adjuncts (cocoa, vanilla, maple, peanut butter). 10%+. Divisive. "Beer-flavored dessert." Omnipollo, Prairie Bomb.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Guinness Draught** — $10/6-pack. The global icon. Coffee, cream, dry finish. 4.2% ABV — lighter than most lagers. "Everyone knows Guinness. Not everyone knows it's only 125 calories per serving."

2. **Founders Breakfast Stout** — $12/4-pack. Oatmeal stout brewed with coffee and chocolate. 8.3% ABV. "Dessert in a glass. Morning optional."

3. **Left Hand Milk Stout (Nitro)** — $10/6-pack. Pour hard into a glass — the cascade is the experience. Sweet, chocolatey, smooth. "The stout for people who think they don't like stout."

4. **North Coast Old Rasputin** — $12/4-pack. Imperial Stout, 9% ABV. Coffee, dark chocolate, tar. "The imperial stout benchmark. Bold, warming, excellent with chocolate desserts."

5. **Goose Island Bourbon County Stout** — $15-20/bottle (annual release, November). THE barrel-aged stout. Bourbon, vanilla, chocolate. "This releases once a year and people line up for it. If we have it, it sells itself."

**WHAT TO SAY ON THE FLOOR**

"Never tried stout? Guinness — it's lighter than you think. 4.2%, creamy, dry."

"Stout lover who wants MORE? Old Rasputin or Founders KBS."

"Pairing: stout + oysters is legendary. Stout + chocolate cake is obvious but perfect."
