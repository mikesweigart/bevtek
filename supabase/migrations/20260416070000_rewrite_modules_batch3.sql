**THE STORY**

Japan's whisky story begins with one man: Masataka Taketsuru. In 1918, this chemistry student traveled from Japan to Scotland to learn distilling. He apprenticed at multiple distilleries, married a Scottish woman named Rita, and returned home in 1920 with the knowledge to build a Japanese whisky industry from scratch.

Taketsuru first helped Shinjiro Torii (founder of Suntory) build the Yamazaki distillery in 1923 — Japan's first. Then Taketsuru left to found his own company: Nikka. These two rivals — Suntory and Nikka — built the entire Japanese whisky industry.

For decades, Japanese whisky was unknown outside Asia. Then in 2001, Nikka's Yoichi won "Best of the Best" at Whisky Magazine's awards. In 2015, Jim Murray's Whisky Bible named Suntory's Yamazaki Sherry Cask 2013 the "World's Best Whisky." The floodgates opened. Global demand exploded overnight.

**WHY IT'S SO EXPENSIVE NOW**

Whisky takes years to age. When demand surged post-2015, Japanese distillers had no surplus aged stock. Age-stated bottles (Yamazaki 12, Hakushu 12, Hibiki 17) became nearly impossible to find. Prices on the secondary market went 5-10x retail. The distillers responded by releasing NAS (No Age Statement) bottles — still excellent, just without the age on the label.

**WHAT MAKES IT DIFFERENT**

Japanese whisky was modeled on Scotch but refined with Japanese precision. Lighter, more nuanced, often floral. Some distilleries use **mizunara** (Japanese oak) casks that add distinctive sandalwood, incense, and coconut notes found nowhere else.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Suntory Toki** — $30. A blend designed for highballs (whisky + soda, the way Japan drinks it). Light, citrus, apple. "The everyday Japanese whisky. Makes a perfect Toki Highball — 1 oz Toki, 4 oz cold soda, big ice."

2. **Nikka From The Barrel** — $65. A blend bottled at 51.4% ABV. Rich, complex, caramel and spice. "The whisky enthusiast's Japanese blend. This wins blind tastings against $150 bottles."

3. **Hibiki Harmony** — $75. Suntory's flagship blend. Rose, lychee, honey, oak. Beautiful 24-faceted bottle (representing the 24 seasons of the Japanese calendar). "The gift bottle. This is what you give someone who deserves something extraordinary."

4. **Yamazaki Distiller's Reserve** — $90. NAS single malt from Japan's first distillery. Cherry, strawberry, cinnamon. Harder to find. "If we have it, it sells."

5. **Hakushu Distiller's Reserve** — $85. "Mountain" single malt, lightly peated, herbaceous, green apple. "Japanese whisky's answer to a light Scotch."

**WHAT TO SAY ON THE FLOOR**

"Want to try Japanese whisky? Toki at $30 — make a highball like they do in Tokyo."

"Serious whisky lover? Nikka From The Barrel. $65. It punches way above its price."

"Gift? Hibiki Harmony. The bottle alone is a work of art. $75."

"Why is everything so expensive? Demand exploded faster than Japan could age whisky. There's literally not enough old stock. This isn't going to change soon."
