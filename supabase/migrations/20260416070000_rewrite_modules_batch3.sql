**THE STORY**

IPA stands for India Pale Ale — a beer style invented by necessity. In the 1700s, British brewers needed to ship beer to soldiers and colonists in India. Regular beer spoiled on the 6-month sea voyage. Solution: add more hops (a natural preservative) and increase the alcohol. The resulting beer was strong, bitter, and hop-forward. It survived the trip and tasted great on arrival.

The American craft beer revolution of the 1980s-90s took this British style and dialed everything to 11. Sierra Nevada Pale Ale (1980), then Stone IPA (1997) defined the West Coast IPA — aggressive, piney, grapefruit-bitter. By the 2010s, New England IPAs (hazy, juicy, tropical) flipped the script again. Today, IPA is the most-ordered craft beer style in America.

**THE STYLES (memorize these — customers ask constantly)**

• **West Coast IPA** — Clear, bitter, pine + grapefruit. 6-7.5% ABV. The original American style. Stone IPA, Ballast Point Sculpin, Lagunitas.
• **Hazy / New England IPA (NEIPA)** — Cloudy, juicy, tropical fruit, SOFT bitterness. 6.5-8%. The dominant style of the 2020s. Sierra Nevada Hazy Little Thing, Voodoo Ranger Haze.
• **Double IPA (DIPA)** — Stronger version of either style. 7.5-10%. Pliny the Elder, Bell's Two Hearted.
• **Session IPA** — Lower alcohol (4-5%), same hop punch. Founders All Day IPA, Lagunitas DayTime.
• **Cold IPA** — Lager-like body + West Coast hops. Crisp, clean, bitter. Newer style.

**THE HOP CHEAT SHEET**

• **Citra** — tropical, mango, passion fruit (the #1 haze hop)
• **Mosaic** — blueberry, tropical, earthy
• **Galaxy** — passionfruit, peach (Australian hop)
• **Simcoe** — pine, earthy, dank
• **Cascade** — grapefruit, floral (the OG American hop — Sierra Nevada uses it)

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Voodoo Ranger Haze** — $10/6-pack. New Belgium's mega-seller. Hazy, tropical, approachable. "The gateway hazy IPA — available everywhere, consistently good."

2. **Sierra Nevada Hazy Little Thing** — $10/6-pack. Sierra Nevada's hazy entry. Mango, peach, oat-soft. "If they liked Blue Moon, they'll love this."

3. **Bell's Two Hearted** — $11/6-pack. American IPA, Centennial hops, perfectly balanced. Named "Best Beer in America" multiple times. "The IPA for people who appreciate balance over intensity."

4. **Founders All Day IPA** — $15/15-pack. Session IPA, 4.7% ABV. Full hop flavor at lunchtime ABV. "Want to drink IPAs all afternoon without falling over? This."

5. **Stone IPA** — $10/6-pack. The West Coast benchmark since 1997. Bold, bitter, grapefruit-pine. "The original. If you like bitter, this is your home."

**WHAT TO SAY ON THE FLOOR**

"'IPAs are too bitter' → try Hazy Little Thing or Voodoo Ranger Haze. Same hops, way less bitter perception."

"'I want the juicy kind' → Hazy IPA. Voodoo Ranger Haze or any NEIPA we carry."

"'I want to try different IPAs for a party' → grab a mix: Hazy Little Thing, Two Hearted, Stone IPA, All Day. Four styles, one 6-pack from each."

"ALWAYS CHECK DATES on IPAs. Anything over 90 days old has lost significant hop character. Fresh IPAs = happy customers."
