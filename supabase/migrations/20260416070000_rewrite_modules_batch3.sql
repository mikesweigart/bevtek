**THE STORY**

Vodka is the world's best-selling spirit — and the most misunderstood. By US law, vodka must be distilled to near-neutral character: "without distinctive character, aroma, taste, or color." This makes people think all vodka is the same. It's not. The base ingredient, water source, and distillation method create real (if subtle) differences.

Vodka's origins are disputed between Russia and Poland — both have been distilling since the 1400s. The word comes from "voda" (water) — "little water." For centuries, it was rough, medicinal, and unrefined. Modern column distillation in the 1800s created the clean, neutral spirit we know today.

**HOW IT CONQUERED AMERICA**

Smirnoff (originally a Russian brand, bought by Americans) cracked the US market in the 1950s with the Moscow Mule — vodka + ginger beer + lime in a copper mug. Brilliant marketing: vodka was positioned as "the spirit that leaves you breathless" (no smell on your breath). James Bond ordered Vodka Martinis. The Cosmopolitan in the 1990s sealed it. By 2000, vodka had overtaken whiskey as America's top spirit.

**BASE INGREDIENTS — WHAT THEY ACTUALLY CHANGE**

• **Wheat** (most common) — soft, slightly sweet, clean. Grey Goose, Absolut, Ketel One, Stolichnaya.
• **Rye** — spicier, firmer, more "grippy." Belvedere, Potocki.
• **Corn** — sweet, round, very smooth. Tito's, Deep Eddy. (Tito's is technically made like bourbon — corn-based, pot-distilled — then filtered to neutral.)
• **Potato** — creamy, full-bodied, slightly earthy. Chopin, Luksusowa.
• **Grape** — silky, delicate, almost perfumed. Cîroc.

**THE PREMIUM DEBATE (be honest with customers):** Blind tastings consistently show most people can't distinguish premium from mid-tier vodka when it's chilled and in cocktails. The differences ARE detectable at room temperature, neat. Premium vodka is often about the bottle and brand for gifting.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Tito's Handmade Vodka** — $22. The #1 vodka in America by volume. Corn-based, Austin, Texas. Clean in cocktails, good Bloody Mary vodka. "The default. When someone says 'vodka' without specifying, this is what they mean."

2. **Grey Goose** — $35. French wheat vodka. The prestige brand. "Gift? Grey Goose. The person receiving it feels special. That's the product — the feeling."

3. **Ketel One** — $25. Dutch, wheat-based, copper-pot distilled. Slightly more character than most. A bartender favorite for Martinis. "A step up from Tito's at the same price point."

4. **Absolut** — $22. Swedish, wheat, iconic bottle design. Dozens of flavors. The cocktail workhorse.

5. **Chopin** — $33. Polish potato vodka. Creamy, fuller body. "For the customer who wants something different — tell them it's potato-based. That starts a conversation."

**WHAT TO SAY ON THE FLOOR**

"Vodka tonic at home? Tito's or Ketel One — both under $25, both clean."

"Gift? Grey Goose. Always. The name does the work."

"Customer says 'all vodka is the same': try Chopin (potato) vs. Grey Goose (wheat) side by side. They'll taste the difference."
