**THE STORY**

Rum is the spirit of the Caribbean — born from sugarcane, forged in the heat of the tropics, and intertwined with 400 years of colonial history. When Columbus brought sugarcane to the Caribbean in 1493, plantation workers discovered that molasses (a byproduct of sugar refining) could be fermented and distilled into spirit. By the 1600s, rum was currency: the British Royal Navy gave sailors a daily rum ration until 1970.

**HOW IT REACHED AMERICA**

Rum was Colonial America's drink of choice — BEFORE bourbon existed. New England rum distilleries imported Caribbean molasses and produced enormous quantities. The Molasses Act of 1733 (a British tax on foreign molasses) was one of the grievances that led to the American Revolution. After independence, whiskey replaced rum as Americans moved inland (away from port cities). Rum's recent American revival started in the 2010s with craft cocktail bars rediscovering tiki culture and classic daiquiris.

**THE THREE STYLE FAMILIES**

• **Spanish-heritage (Cuban-style)** — column-distilled molasses. Light, clean, mixable. Bacardi, Flor de Caña, Diplomático, Ron Zacapa.
• **English-heritage (Jamaican/Bajan)** — pot-distilled molasses. Funky, rich, tropical fruit esters (banana, pineapple). Appleton Estate, Mount Gay, Smith & Cross.
• **French-heritage (Rhum Agricole)** — fresh sugarcane juice (not molasses). Grassy, vegetal, herbal. Rhum Clément, Rhum JM, Barbancourt.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Bacardi Superior** — $15. The world's best-selling rum. Light, clean, neutral. "Mojito and Daiquiri rum. The workhorse."

2. **Diplomático Reserva Exclusiva** — $40. Venezuelan, solera-aged, rich caramel and toffee. "Sipping rum for bourbon lovers. Hand someone a glass neat — they'll think it's aged whiskey."

3. **Appleton Estate 12** — $30. Jamaican pot-still magic. Orange, toffee, tropical funk. "The bartender's rum. Makes the best Mai Tais."

4. **Captain Morgan Original** — $18. Spiced rum. Vanilla, cinnamon, easy with cola. "Not for cocktail purists, but it's what 60% of rum buyers actually want."

5. **Mount Gay Eclipse** — $22. From Barbados, the oldest rum-producing island (since 1703). Golden, balanced, versatile. "History in a bottle."

**WHAT TO SAY ON THE FLOOR**

"Mojito/Daiquiri? Bacardi or Plantation 3 Stars — white, clean, lets the lime and mint shine."

"Customer wants to SIP rum like whiskey? Diplomático Reserva or Appleton 12. No mixer needed."

"Gift for a rum lover? El Dorado 15 ($40) or Zacapa 23 ($50) — both gorgeous bottles, both sipping-quality."
