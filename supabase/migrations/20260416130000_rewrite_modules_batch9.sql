**WHY LABELS CONFUSE EVERYONE**

Wine labels are designed by winemakers, not customers. They assume you already know what "Appellation d'Origine Contrôlée" means or that "Barolo" implies Nebbiolo. Your staff needs to decode these for the 90% of customers who feel lost.

**THE BIG DIVIDE: OLD WORLD vs. NEW WORLD**

**New World labels** (USA, Australia, NZ, Chile, Argentina) lead with the **GRAPE NAME**: "2022 Napa Valley Cabernet Sauvignon." The grape is front and center.

**Old World labels** (France, Italy, Spain, Germany) lead with the **REGION**: "Chablis 2021." The grape (Chardonnay) is NEVER stated — it's implied by the region. This is the #1 source of confusion for American customers.

**CHEAT SHEET: Region → Grape**
• Chablis = Chardonnay
• Sancerre = Sauvignon Blanc
• Burgundy Red = Pinot Noir
• Bordeaux Red = Cabernet/Merlot blend
• Chianti = Sangiovese
• Barolo = Nebbiolo
• Rioja = Tempranillo

**TERMS THAT ACTUALLY MATTER**

• **Vintage** (year) — important for quality variation in cool climates (Burgundy, Bordeaux). Less important in consistent warm climates (California, Australia).
• **ABV** — tells you body. Under 12% = lighter. 14-15% = full and rich.
• **"Reserve" / "Reserva" / "Riserva"** — means DIFFERENT things in different countries. In Spain/Italy: legally defined aging requirements. In the US: MEANINGLESS marketing. Teach your staff this.
• **"Estate Bottled"** — made from grapes grown and bottled by the same producer. Generally a quality signal.
• **Sulfites** — legally required on all US-sold wine. ALL wine contains sulfites. This is NOT a quality or health red flag.

**WHAT TO SAY ON THE FLOOR**

Customer staring at a French label looking confused → "That's a Sancerre — it's Sauvignon Blanc from the Loire Valley. France puts the region instead of the grape on the label."

"See where it says 'Crianza'? That means it's been oak-aged at least one year. 'Reserva' means longer aging. The tier tells you how much time the winery invested."
