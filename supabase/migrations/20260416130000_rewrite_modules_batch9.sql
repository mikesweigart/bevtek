**THE STORY**

For most of brewing history, all beer was at least a little sour — wild yeast and bacteria were unavoidable. Louis Pasteur's discovery of pasteurization in the 1860s allowed brewers to control fermentation and eliminate unwanted sourness. "Clean" beer became the standard.

But some traditions preserved intentional sourness: Belgian lambics (spontaneously fermented in open vats), German Goses (salty, sour wheat beers from Leipzig), and Flanders reds (aged sour ales from Belgium). These styles nearly went extinct in the 20th century before craft brewers rediscovered and celebrated them.

Today, sour beer is a $1B+ category. Fruited sours, in particular, have become gateway beers for people who "don't like beer."

**THE STYLES**

• **Berliner Weisse** — light, tart, low ABV (2.5-4%). Wheat base. Often served with fruit syrup. "Refreshing like lemonade."
• **Gose (GO-zuh)** — tart + salty + coriander. 4-5% ABV. "The savory sour. The salt makes it weirdly addictive."
• **Lambic / Gueuze** — Belgian spontaneous fermentation, aged in oak. Complex, funky, cidery. Cantillon, Drie Fonteinen.
• **Kriek** — lambic fermented with whole cherries. Tart cherry, almond, not sweet.
• **Flanders Red** — aged sour red ale. Wine-like, tart, caramel. Rodenbach Grand Cru, Duchesse de Bourgogne.
• **Fruited Sour** — American craft category. Sour base + heavy fruit additions (mango, passion fruit, raspberry). Sweet-tart, smoothie-like.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Westbrook Gose** — $10/6-pack. The American Gose benchmark. Salty, tart, perfect summer beer. "The gateway sour."

2. **Duchesse de Bourgogne** — $8/330ml. Flanders Red. Tart, caramel, wine-like. "The sour for wine drinkers."

3. **Anderson Valley Gose** — $10/6-pack. Blood orange variety is popular. "Approachable, citrusy, crushable."

4. **Lindemans Framboise** — $8/375ml. Raspberry lambic. Sweet-tart, pink, crowd-pleasing. "The one people who 'hate beer' actually love."

5. **Creature Comforts Athena Paradiso** — $14/4-pack. Fruited Berliner Weisse. Tropical, tart, rotating fruits. "The brewery that made Athens, GA famous — beyond Tropicália."

**WHAT TO SAY ON THE FLOOR**

"'I don't like beer' → try a fruited sour or Lindemans Framboise. They taste like tart fruit juice."

"Hot summer day, want something light? Gose — salty, tart, 4% ABV. Session it."

"Wine drinker curious about beer? Duchesse de Bourgogne — it literally tastes like sour red wine."
