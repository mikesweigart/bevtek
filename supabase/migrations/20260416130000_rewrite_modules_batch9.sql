**THE STORY**

Dessert wines are the world's most underappreciated category. They include some of the longest-lived, most complex wines ever made — yet most customers have never tasted a good one. The reason: dessert wine disappeared from American culture when cocktails and spirits took over after-dinner drinking. But in Europe, finishing a meal with Sauternes, Port, or Tokaji is as natural as coffee.

Your store probably has dessert wines collecting dust. This module will help you SELL them.

**THE STYLES**

**Port (Portugal):**
• **Ruby** — young, fruity, vibrant. $12-20. Entry level.
• **Tawny 10/20/30/40 Year** — barrel-aged. Caramel, nuts, dried fruit. 20-year ($35-50) is the sweet spot for gifts.
• **Vintage Port** — declared only in great vintages. Massive, needs decanting. $40-150.
• **LBV (Late Bottled Vintage)** — ready-to-drink "mini Vintage Port." $15-25.

**Sauternes (France):**
Noble-rot (Botrytis) Sémillon from Bordeaux. Honeyed, apricot, marmalade. Pairs with foie gras and blue cheese. Château d'Yquem ($200+) is the icon. Château Rieussec ($40-80) is excellent.

**Ice Wine / Eiswein (Canada, Germany):**
Grapes frozen on the vine, pressed while frozen. Intensely sweet, piercing acid. Apricot, lychee, honey. Inniskillin ($40-60 for 375ml). German Eiswein from Mosel.

**Moscato d'Asti (Italy):**
Lightly sparkling, barely alcoholic (5-6% ABV). Peach, apricot, honey. $12-20. "Wine for people who think they don't like wine."

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Taylor Fladgate 20 Year Tawny Port** — $40. THE gift Port. Caramel, nuts, toffee, butterscotch. "After-dinner with chocolate? Nothing beats 20-year Tawny."

2. **Graham's Six Grapes Ruby Port** — $18. Entry-level but excellent. Fruity, rich, easy to love. "Port curious? Start here."

3. **Château Rieussec Sauternes** — $45 (375ml). Bordeaux. Golden, honeyed, complex. "The blue-cheese pairing wine."

4. **Inniskillin Vidal Ice Wine** — $50 (375ml). Canadian. Intensely sweet + acid. "The wine that makes people's eyes go wide on the first sip."

5. **Michele Chiarlo Moscato d'Asti** — $15. Italian. Light, bubbly, peachy. "Brunch wine or dessert wine — your call."

**WHAT TO SAY ON THE FLOOR**

"Chocolate dessert? Tawny Port. It's the pairing that's been working for 300 years."

"Gift for a wine lover who has everything? 375ml of Sauternes or Ice Wine. Small bottle, HUGE impression."

"Customer says 'I don't drink sweet wine': 'Have you tried GOOD sweet wine? Sauternes has more acid than most dry whites. It's sweet but not syrupy.'"
