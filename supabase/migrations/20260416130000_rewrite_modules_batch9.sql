**THE STORY**

Lager is the most consumed alcoholic beverage on Earth. Over 90% of all beer sold worldwide is lager — from Bud Light to Pilsner Urquell, from Modelo to Augustiner. The word "lager" comes from the German "lagern" (to store), because these beers are cold-aged for weeks to months after fermentation, producing a cleaner, crisper result than ales.

The style was revolutionized in 1842 when Bavarian brewer Josef Groll created the first golden lager in Plzeň, Czech Republic — the original Pilsner. Before that, most beer was dark and cloudy. Pilsner's clarity and crispness shocked the world. Within decades, it became the template for modern beer worldwide.

**THE STYLES**

• **German Pilsner** — crisp, dry, floral hops. Bitburger, Warsteiner, Jever (extra hoppy). "Precise, clean, engineered."
• **Czech Pilsner** — slightly richer, more malt body. Pilsner Urquell (the OG), Budvar. "Softer, breadier, with a biscuit backbone."
• **Helles** — Munich-style, soft, malty, low bitterness. Augustiner, Weihenstephaner, Hofbräu. "Better Bud Light."
• **Märzen / Oktoberfest** — amber, malty, caramel. Brewed in spring, drunk in fall. Paulaner, Hacker-Pschorr.
• **Mexican Lager** — Modelo Especial, Pacifico, Victoria. Clean, light, versatile. "Tacos, lime, done."
• **American Lager** — Budweiser, Coors, Miller. Light, adjunct (corn/rice), crushable.
• **Italian Pilsner** — craft trend: dry-hopped pilsner. Crisp + aromatic.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Modelo Especial** — $10/6-pack. The #1 beer in America (surpassed Bud Light in 2023). Clean, crisp, versatile. "The people's beer. Goes with everything."

2. **Pilsner Urquell** — $10/6-pack. The original pilsner, since 1842. Biscuity, floral, balanced. "Drinking history."

3. **Augustiner Helles** — $14/6-pack. Munich's beloved lager. Soft, honeyed, pure. "The Helles that Bavarians drink."

4. **Founders Solid Gold Lager** — $10/6-pack. American craft lager. Clean, drinkable, session-worthy. "Craft credibility, lager drinkability."

5. **Pacifico** — $10/6-pack. Mexican pilsner. Light, crisp, lime-friendly. "Beach beer. No explanation needed."

**WHAT TO SAY ON THE FLOOR**

"Bud Light drinker who's curious? Founders Solid Gold or Augustiner Helles — same easy-drinking vibe, exponentially more flavor."

"Oktoberfest season? Stock Märzens heavily — customers literally walk in asking for 'Oktoberfest beer.'"

"Mexican food tonight? Modelo or Pacifico with a lime. The pairing is cultural."
