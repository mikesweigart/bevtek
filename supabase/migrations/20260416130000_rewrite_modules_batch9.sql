**THE STORY**

Tennessee Whiskey is bourbon's next-door neighbor — legally, it meets every bourbon requirement, plus one extra step: the **Lincoln County Process**. Before going into the barrel, the new-make spirit drips through 10 feet of sugar maple charcoal. This takes 3-5 days and removes harsh flavors while adding a subtle sweetness and char character.

Jack Daniel's — the world's best-selling American whiskey — is the reason most people know Tennessee Whiskey exists. Founded in 1866 by Jasper Newton "Jack" Daniel in Lynchburg, Tennessee (population: 361), the distillery now produces over 12 million cases per year. Lynchburg, ironically, is in a dry county — you can't buy a drink there.

**THE NATHAN GREEN STORY**

In 2017, Jack Daniel's officially acknowledged that Jack Daniel learned distilling from Nathan "Nearest" Green — an enslaved man who was the master distiller at the farm where young Jack apprenticed. Uncle Nearest Premium Whiskey was founded to honor Green's legacy, and it's now one of the fastest-growing brands in American whiskey.

**TOP SELLERS YOUR STAFF MUST KNOW**

1. **Jack Daniel's Old No. 7** — $25. The icon. Black label, square bottle, worldwide recognition. Light banana, caramel, light smoke. "Everyone's had it. Not everyone knows it's NOT bourbon — it's Tennessee Whiskey."

2. **Jack Daniel's Single Barrel Select** — $50. Individual barrels, 94 proof. Richer, more complex, still approachable. "The upgrade from Old No. 7 that feels like a different whiskey."

3. **Uncle Nearest 1856 Premium** — $60. 100 proof, blend of 8-14 year barrels. Rich, complex, award-winning. "The historical tribute with substance behind the story."

4. **George Dickel Bottled in Bond** — $40. 13 years old, 100 proof. Chilled charcoal mellowing. "The whiskey-geek's Tennessee pick. Insane value at $40 for 13 years."

5. **Gentleman Jack** — $30. Double charcoal-filtered (twice through the Lincoln County Process). Extra smooth. "Jack Daniel's but smoother — for people who find Old No. 7 a little rough."

**WHAT TO SAY ON THE FLOOR**

"Bourbon or Tennessee? Tennessee goes through charcoal mellowing — makes it smoother. If they want easy sipping, Tennessee."

"Gift? Jack Daniel's Single Barrel Select, $50. Same brand everyone knows, elevated execution."

"Want to learn the real history? Uncle Nearest — named after the man who actually taught Jack Daniel to distill."
