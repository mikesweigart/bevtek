**WHY PLACEMENT = SALES**

In retail, where a product sits determines whether it sells. Products at eye level sell 30-40% more than the same product on the bottom shelf. End-cap displays sell 5-10x their normal rate. Your shelf strategy directly impacts store revenue — and your commission if your store has one.

**THE RULES**

**Eye level = buy level.** Products at 4-5 feet (average adult eye height) get the most visual attention. Place your highest-margin and most-recommended products here. Budget brands go lower; premium goes at eye level.

**The "V" scan pattern.** Customers' eyes naturally scan shelves in a V: upper-left → center → upper-right. Place best-sellers and featured items in this path.

**End caps sell.** Product displays at the end of an aisle get 5-10x the attention of in-aisle placement. Use for seasonal items, sale products, and staff picks.

**CROSS-MERCHANDISING**

This is the highest-impact thing a staff member can do:
• **Bitters + Luxardo cherries + simple syrup** near the bourbon
• **Tonic water + limes** near the gin
• **Margarita salt + limes** near the tequila
• **Ginger beer** near the vodka (Moscow Mules)
• **Coco López + pineapple juice** near the rum (Piña Coladas)
• **Gift bags + tissue paper** near the Champagne during holidays

Every cross-merchandised display increases average basket size by suggesting the COMPLETE experience, not just a single bottle.

**SIGNAGE**

• **"Staff Pick" cards** — handwritten one-line tasting note. Feels authentic, drives trust.
• **"Pairs with..."** cards — "This Malbec pairs with steak, burgers, and hard cheese."
• **Price ALWAYS visible** — customers who can't see the price won't ask. They'll leave.
• **QR code to your Megan Shopper page** — for customers who want to browse more at home.

**WHAT TO SAY ON THE FLOOR**

Walk your store like a customer once a week. What grabs your eye? Where do people hesitate? Fix one thing each walk-through.

If you notice a product not selling, check its placement before blaming the product. Move it to eye level or an end cap — it might fly.
