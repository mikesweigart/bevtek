**WHY EVERY STAFF MEMBER NEEDS TO KNOW THIS**

Inventory management isn't just the manager's job. Every staff member who touches a delivery, stocks a shelf, or notices a low product is part of the system. Good inventory practices = products on shelves when customers want them. Bad practices = empty shelves, frustrated customers, and lost revenue.

**RECEIVING DELIVERIES**

1. **Check the delivery against the purchase order (PO).** Count the boxes. Look for damage. Check that what's on the truck matches what was ordered.
2. **Note discrepancies BEFORE the driver leaves.** Short a case of Maker's Mark? Wrong vintage of Caymus? Write it on the receipt before you sign. After the driver leaves, it's your problem.
3. **Date-stamp the cases.** Use a marker: "Received 4/16." This helps with FIFO (see below).
4. **Move product to the floor or stockroom promptly.** Don't leave pallets blocking aisles or back doors.

**FIFO — First In, First Out**

The most important inventory rule in any retail environment:
• Older stock goes to the FRONT of the shelf
• New stock goes BEHIND the older stock
• This prevents: stale IPAs (hops die after 90 days), dusty bottles customers perceive as "old," and expired products

For beer especially: FIFO is non-negotiable. A 4-month-old IPA has lost half its hop character.

**CYCLE COUNTS**

Full inventory counts are painful and infrequent. **Cycle counts** are faster and catch problems sooner:
• Count ONE section per week (all bourbon this week, all California wine next week)
• Compare your count to the POS system
• Discrepancies = shrinkage: theft, breakage, or counting errors
• Industry average shrinkage: 2-3% of sales

**BREAKAGE**

• Report IMMEDIATELY — don't hide it. A written-off bottle is better than a mystery gap in inventory.
• Glass cleanup: sweep, then wet-mop. Tiny shards hide in crevices.
• Some distributors accept returns for damaged goods — ask your manager.

