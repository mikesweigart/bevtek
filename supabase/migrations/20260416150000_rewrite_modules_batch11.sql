**WHY HOLIDAYS MAKE OR BREAK YOUR YEAR**

November-December represents 30-40% of annual revenue for most liquor stores. The customer who buys one bottle a month suddenly buys six — for Thanksgiving dinner, Christmas gifts, New Year's Eve, and the company holiday party. Staff who are prepared for this surge (not just stocking, but SELLING) capture the opportunity. Staff who aren't prepared watch it walk out the door.

**THANKSGIVING WEEK (your single biggest week)**

Stock heavy on:
• **Pinot Noir** — Oregon ($18-25) and Burgundy ($25-50). THE Thanksgiving red.
• **Beaujolais Nouveau** — releases the 3rd Thursday of November. Fun, festive, $12-15.
• **Riesling** — off-dry German ($15-20). Handles turkey, cranberry, AND sweet potatoes.
• **Sparkling** — Prosecco for aperitif, Champagne for the toast.
• **Bourbon + rye** — "Friendsgiving" cocktails (Old Fashioneds, Manhattans).

Shelf talker: "Thanksgiving wines — our staff picks."

The Wednesday before Thanksgiving is the biggest single sales day of the year. Schedule extra staff. Open early if you can.

**DECEMBER / CHRISTMAS / HANUKKAH**

Gift sets and premium bottles dominate. Key categories:
• **Champagne / sparkling** — outsells everything else by revenue
• **Premium whiskey** — Woodford Reserve, Blanton's (if allocated), WhistlePig
• **Gift liqueurs** — Baileys, Grand Marnier, Amaretto, hot chocolate kits
• **Gift bags + tissue paper** — stock HEAVILY by December 1

**NEW YEAR'S EVE**

Champagne. Stock 3x your normal amount. La Marca Prosecco for budget, Veuve Clicquot for prestige. People WILL make a late-night run for "one more bottle" — consider extending hours.

**JANUARY (Dry January)**

Pivot don't panic. Stock NA beer (Athletic Brewing) and NA spirits (Seedlip, Lyre's). Shelf talker: "Taking a break? Try these." Your regular customers come back in February. The NA buyers are NEW customers who might become regulars.

**THE GOLDEN RULE**

Holiday shoppers are in a GOOD MOOD. They want help, they're open to suggestions, and they'll spend more than planned if guided. This is the easiest selling season of the year.

**WHAT TO SAY ON THE FLOOR**

Monday before Thanksgiving: "Getting everything for Thursday? Can I help you pick the wines?"

December gift shopper: "Who's it for? What's the budget? I've got the perfect thing."

New Year's Eve afternoon: "Stocking up? Grab an extra bottle — you'll thank me at 11pm."
