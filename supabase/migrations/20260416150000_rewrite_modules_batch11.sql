**THIS MODULE COULD SAVE YOUR JOB (AND YOUR FREEDOM)**

Selling alcohol to a minor is a criminal offense in every US state. The liability falls on BOTH the individual employee AND the store. Consequences for the employee can include:
• **Fines** — $500 to $10,000+ depending on state
• **Criminal misdemeanor charge** — yes, YOU personally, not just the store
• **Termination** — immediate in most stores
• **Loss of ability to work in alcohol retail** — some states bar you
• **Store's license suspension or revocation** — which means everyone loses their job

This is not theoretical. ABC (Alcohol Beverage Control) agents run sting operations constantly.

**THE STING OPERATION**

Here's exactly how it works:
1. An ABC agent sends an actual minor (typically 18-20) into your store
2. The minor presents their REAL ID — which is a vertical (under-21 format) showing an under-21 birthdate
3. They DO NOT lie about their age if asked. They show the real ID.
4. If you don't check the ID, or check it and sell anyway, you're cited on the spot
5. The fine and potential criminal charge happen TO YOU

**THE RULES (memorize these)**

• **21 or older to purchase** — no exceptions anywhere in the US
• **Acceptable ID:** state driver's license, state ID card, US passport, US military ID
• **Check everyone who looks under 40** (some stores say under 30 — follow YOUR store's policy)
• **Expired ID = not valid** in most jurisdictions
• **No ID = no sale. Period.** "Store policy" is all you need to say.
• **Straw purchases** — if you see an adult buying for a minor (passes the bag, minor waiting outside), refuse the sale

**WHAT TO SAY ON THE FLOOR**

"Can I see your ID? Thanks — we check everyone." (Smile. Normal. No big deal.)

Customer forgot ID: "I totally understand — I need valid ID to sell alcohol though. You're welcome to come back with it."

Customer gets annoyed: "I know it's a hassle — I appreciate your patience. It protects both of us."

Customer says "I'm clearly over 21": "You definitely look it — it's just our policy for everyone under 40. Takes 5 seconds."
