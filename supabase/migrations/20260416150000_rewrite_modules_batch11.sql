**WHY THIS TRAINING MATTERS**

Every retail employee encounters difficult situations. In liquor retail specifically, you deal with intoxicated customers, underage purchase attempts, angry complaints, and occasionally threatening behavior. How you handle these moments protects your safety, your job, and your store's license.

**REFUSING AN INTOXICATED CUSTOMER**

You are **LEGALLY REQUIRED** in most states to refuse sale to visibly intoxicated people. This is not optional. This is the law.

**How to refuse:**
1. Be calm, direct, and non-judgmental
2. "I can't sell alcohol to you right now. You're welcome to come back another time."
3. Don't negotiate. Don't over-explain. State the refusal once and hold.
4. If they escalate → call your manager
5. If they threaten → call police. Your safety comes first. ALWAYS.

**ID VERIFICATION**

• Check everyone who looks under 40
• Expired ID = not valid in most states. Refuse politely.
• Vertical (under-21 format) IDs: legal after the person turns 21, but double-check the birthdate carefully
• No ID = no sale. "Store policy" is your shield. Don't budge.

**THE ANGRY CUSTOMER (general)**

Most angry customers just want to be HEARD. The framework:
1. **Listen** — let them vent for 30 seconds. Don't interrupt.
2. **Acknowledge** — "I understand that's frustrating."
3. **Solve** — "Here's what I can do for you..."
4. **Don't take it personally** — it's almost never about you.

90% of angry customers de-escalate once they feel heard. The other 10% need a manager.

**PRICE COMPLAINTS**

"This is cheaper at [competitor/online]."

Your answer: "I understand — we focus on selection and service. Happy to help you find something in your budget though." Don't bad-mouth competitors. Don't match prices unless your store policy says to.

**THE SAFETY MANTRA**

You're a retail employee, not a security guard. If someone is:
• Shoplifting → note their description, tell manager, don't physically confront
• Threatening → de-escalate if safe, retreat if not, call 911
• Violent → get away. Get everyone away. Call police.

Money and inventory are replaceable. You are not.

**WHAT TO SAY ON THE FLOOR**

Intoxicated customer: "I appreciate you coming in — I'm not able to sell you alcohol right now, but you're welcome back anytime."

Angry about a return: "I hear you — let me see what I can do. Can you tell me what happened?"

No ID: "I'd love to help you — I just need to see a valid ID first. It's store policy for everyone."
